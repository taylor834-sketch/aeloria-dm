# Harwick Holds the Line
### *A village that already said no. Now it has to mean it.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Highfeld farming country, three days east of Dunmore |
| **Level Range** | 3–6 |
| **Duration** | 2–3 sessions |
| **Quest Giver** | Bram Ashely, village headman |
| **Quest Type** | Political — Defense, Alliance-Building, Ticking Clock |

---

## THE SITUATION

The village of Harwick sent Blackwell's grain requisition order back unsigned three weeks ago. Headman Bram Ashely drafted the refusal letter himself, sealed it with Harwick's modest civic stamp, and dispatched it on the morning courier. He has not regretted this. He has, however, been watching the road south every morning since.

Harwick is a farming village of perhaps three hundred people. It cannot defeat forty soldiers. What it can do — what Bram is hoping, quietly and desperately, it can do — is make the cost of retribution look high enough that Blackwell redirects his attention to softer targets. Two neighboring villages, Coldbridge and Welling, are watching Harwick's situation with close attention. Their headmen have made informal contact with Bram: if Harwick holds, they will follow. If Harwick breaks, they will sign whatever is put in front of them.

The players arrive with approximately two weeks before Blackwell's punitive force reaches the area.

---

## BACKGROUND

*(DM-only — do not share with players)*

Blackwell's order is legally questionable. The Aranthor Charter of 847 limits military grain seizure to declared wartime and requires sign-off from at least three lords of the Council. Blackwell has declared a "stability emergency" — a status he invented — and obtained signatures from two compliant lords. The legal argument against him is real but requires someone willing to make it formally, in writing, before witnesses.

The punitive force is forty cavalry under a captain named Edric Holt — a professional, not a sadist. Holt has done this before. His standard approach: arrive in force, assess the mood, make an example of the refusal in the most efficient way possible. He does not enjoy burning things but he will if the math supports it. He can be negotiated with, stalled, or made to look at a better option.

Bram's real hand: the grain stores. Harwick's winter stores are large — a bumper harvest last year. If those stores are moved, hidden, or made unavailable before Holt arrives, there is nothing to seize. The requisition order becomes moot. Holt may burn things in frustration, but burning an empty storehouse carries different optics than a successful seizure.

A successful defense here does not just protect Harwick. It ignites something. Within three days of word reaching Coldbridge and Welling, both villages send refusals of their own. Within a week, two more Highfeld villages follow. This is the kind of resistance that makes Blackwell redirect resources — and draws Elowen's network's serious attention.

---

## KEY NPCs

### Bram Ashely — Village Headman
**Appearance:** Mid-fifties, broad through the shoulders from a lifetime of farm work, with close-cropped grey hair and hands that are always slightly dirty. He has a scar across his right palm from cavalry service twenty years ago.

**Manner:** Calm and deliberate to the point of seeming slow. He isn't slow. He has learned that his first response to any problem is rarely his best one, so he waits. In conversation he listens more than he speaks.

**Wants:** For Harwick to survive this with its dignity intact. Not to win. To survive without becoming something it isn't.

**Secret:** The neighboring villages are waiting on Harwick's result. Bram knows this and it weighs on him — he did not make this decision on behalf of three villages, but it has become that. He will tell the players if they earn his trust, because he knows they need the full picture.

**Roleplaying note:** Bram is frightened and committed, in that specific combination that holds under moderate pressure but will crack under serious force. He needs the players to take the fear he is carrying and give him something to do with it. He won't perform courage. He'll perform competence, which is close enough.

### Captain Edric Holt — Blackwell Punitive Force
**Appearance:** Forty, lean, has the unhurried movement of someone who stopped being nervous about this work a long time ago. Keeps his uniform clean in the field.

**Manner:** Professionally courteous. He does not negotiate out of sentiment; he negotiates because it is sometimes more efficient than fighting.

**Wants:** To complete the requisition and leave. A clean outcome with no unnecessary property damage.

**Secret:** He has documented three instances where Blackwell's requisition orders exceeded legal authority. The documents are in his kit, kept as insurance, not yet weaponized.

---

## RUNNING IT

### Scene 1: Arriving in Harwick

> The road into Harwick runs between plowed fields just starting to show green. The village itself is tidy — a proper market square, a well-maintained granary, a Temple of Thyren with a freshly painted door. It looks like a place that keeps itself together. The first person you see is a man sitting on the granary steps reading a letter, or re-reading one. He looks up before you've said anything, like he's been listening for footsteps for several days.

This is Bram. He will ask the players, within two minutes of introductions, whether they have any experience with military logistics, legal procedure, or "making a bad situation look less worth fixing." He is not self-pitying about it. He needs tools and he wants to know which ones he has.

### Scene 2: Building the Defense

The players have roughly two weeks and multiple viable approaches. Bram knows the terrain, the neighbors, and the law. He does not know how to use these things effectively against a cavalry force. Options:

**The Legal Play:** Someone must draft a formal challenge to the requisition order's legality under the Aranthor Charter of 847, deliver it to a Council lord willing to countersign, and have it returned before Holt arrives. This doesn't stop Holt immediately, but it gives him a reason to delay pending "resolution" — soldiers follow orders, and an order under formal legal challenge is a political risk to act on.

**The Hidden Grain:** Move Harwick's winter stores out of the granary and disperse them across a dozen farms in the surrounding countryside. Hard work, requires the village's cooperation, and leaves Holt with nothing to take. The risk: burning the empty granary is Holt's face-saving option.

**The Coalition:** Ride to Coldbridge and Welling, meet their headmen, and formalize the coordination. If all three villages present a joint refusal backed by a written pledge of mutual defense, the requisition becomes a political incident rather than a routine enforcement action.

**The Trap:** Use the grain stores as bait. Allow Holt to believe the stores are present, arrange an ambush or an escape route, and in the resulting chaos, move the stores and disappear before he can reorganize. High-risk, requires military thinking.

### Scene 3: Holt Arrives

> The dust of the column appears on the south road around midmorning — you can see the pennants first, then the horses, then the numbers. Forty of them. They ride in good order. Their captain pulls up a hundred yards from the village square and sits his horse without moving for a long moment, like he's reading the situation.

Holt will send a messenger with a formal request to "complete the chartered requisition." He gives Harwick until the next morning. This is the deadline. The players' preparations determine what options are available.

| DC | Skill | Outcome |
|----|-------|---------|
| 12 | Persuasion (buy Holt's stated window of patience) | He agrees to wait an additional half-day; no visible advantage, but buys movement |
| 15 | Persuasion (legal argument, charter citation) | Holt goes quiet; asks his own legal-minded sergeant; agrees to "await clarification" |
| 14 | Insight (read Holt's actual motivations) | Players identify that he doesn't want a fight — he wants a clean result he can report |
| 16 | Persuasion (present the coalition's joint refusal) | Holt recognizes this is above his operational authority; sends a rider south for instructions |
| 18 | Investigation or Persuasion (discover and leverage Holt's documentation) | Holt agrees to withdraw and "report the situation requires further legal review" |

**Resolution Path A — Legal Victory:** The formal challenge, delivered in time, gives Holt a legitimate reason to disengage. He does so without burning anything. He is not pleased but he is professional.

**Resolution Path B — Empty Granary:** Holt seizes the granary, finds nothing, burns the empty building to preserve the optics of enforcement, and rides south. Harwick loses a building. It keeps its people, its stores, and its dignity. This counts as a win.

---

## IF IT FAILS

If the players fail to provide Harwick with a working defense — no legal argument, no hidden grain, no coalition, no leverage on Holt — the requisition proceeds. The stores are taken. Harwick loses its winter margin. Coldbridge and Welling sign immediately. The cascade of resistance does not happen. Bram is not broken by this; he is diminished in a way that is quiet and permanent.

The players can still encounter the consequence three weeks later: word reaches them that Bram was arrested on a "sedition" charge — Holt made the arrest on his way back through as an object lesson. He's being held in Dunmore. A rescue is possible. It's a different quest.

---

## REWARDS

- 200–400 gp in farm goods, honest coin, and a promissory note from the Highfeld farmers' collective (redeemable in Dunmore, if the players are careful about who they approach)
- Bram's trust, which carries weight in five Highfeld villages
- A copy of the Aranthor Charter of 847, annotated in a retired clerk's hand — useful for future legal challenges to Blackwell's overreach
- If the coalition forms: informal access to the Highfeld Resistance, a network of villages that can shelter, feed, and pass messages across eastern Aranthor

---

## CONNECTS TO

- **The Cracked Crown** — a cascade of village refusals in Highfeld changes Blackwell's strategic calculus; he cannot march on Vaelthorn with a restive supply region behind him
- **Notice at Thornfield** — the same regional intelligence network that alerted Blackwell to Thornwick's bell is paying attention to Harwick's refusal; two connected situations
- **Elowen's Underground** — a successful defense here gets reported to Elowen's network within a week; this is the kind of organized civilian resistance she has been trying to cultivate
