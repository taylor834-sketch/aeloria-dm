# The Notice at Thornfield
### *The report has already been sent. The question now is what gets sent next.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Thornfield, a crossroads village two days from Vaelthorn |
| **Level Range** | 4–6 |
| **Duration** | 1–2 sessions |
| **Quest Giver** | Information encounter — no formal giver |
| **Quest Type** | Counter-intelligence — Information Control, NPC Leverage |

---

## THE SITUATION

A traveling spice merchant named Davan Cross was in Thornfield's common room when the headman, Osric Thale, made the mistake of sharing what he'd heard about Thornwick's bell going silent. Thale is the kind of man who processes worry by talking — he told everyone at the table, without apparent concern for who was listening.

Cross was listening. Cross is a Shadow Cabal courier who works the Vaelthorn-to-Dunwater road under a legitimate trading cover he has maintained for four years. He sent his report up the chain the next morning via a contact at the Split Crown inn, two hours' ride west. The chain leads to a mid-rank Cabal operative who relays to the Thornmere cell and to Blackwell's intelligence section — the two organizations share more information than either publicly admits.

By the time players learn about this, the report has been in Cabal hands for three days. Blackwell's intelligence captain in Dunmore has received a summary. They are paying attention to Thornwick. The silence cannot be unheard. But the players can control what gets believed about it.

---

## BACKGROUND

*(DM-only — do not share with players)*

Killing Cross does not fix the problem. His report was sent before players can reasonably intercept him, and his handlers will notice his disappearance faster than a live Cross can be turned or managed. The useful plays involve what gets sent *next*.

Cross is not ideologically committed to the Cabal. He works for money. He has been a courier for six years because the pay is reliable and the instructions have never crossed lines he cared about crossing. He does not know what the Cabal is actually doing — he carries coded messages and receives payment. He knows the names of two contacts: the woman at the Split Crown (his handler, operational alias "Marlowe") and a Vaelthorn merchant he's never met but delivers to once a month.

Marlowe at the Split Crown is a mid-rank Cabal operative. She has been in place for three years. Her actual identity is Fenra Veil, a former royal court secretary who was recruited after the old king's death. She is smart, careful, and has a secondary data-gathering role — she also collects intelligence for Caedric Valemont's network, which is a side arrangement her Cabal handlers do not know about.

The Blackwell intelligence section interest is the more immediate danger. A Blackwell intelligence officer named Captain Durn is now actively watching the roads out of Thornwick and has asked his informant network to flag any travelers who discuss the bell.

**The opportunity:** If the players can feed Blackwell and the Cabal accurate-but-misleading information about why Thornwick's bell is silent — a mundane mechanical failure, perhaps, documented with forged papers; or a rival faction's false-flag operation — they can defuse the intelligence focus on Thornwick long enough for whatever situation there is developing to complete. Cross, if turned, can carry this disinformation upward.

---

## KEY NPCs

### Davan Cross — Cabal Courier
**Appearance:** Mid-forties, medium everything — medium height, medium build, brown hair going to grey. He has spent years practicing being forgettable and has achieved it.

**Manner:** Smooth. He is good at conversation — interested, curious, never prying. He knows how to make people feel comfortable talking.

**Wants:** Money. A continued safe professional existence. Not to be personally endangered by the work he's been doing.

**Secret:** He has already sent the Thornwick report. He also has, in his saddlebag, a coded message from Marlowe to the Vaelthorn merchant that he hasn't delivered yet. This message, if intercepted and decoded, contains intelligence about Caedric's network that Marlowe was not supposed to be collecting.

**Leverage:** The players can turn Cross if they can demonstrate three things: that they know what he is, that they have evidence that could be used against him, and that they're offering something better than his current arrangement. He is loyal to money, not ideology. A better-paying arrangement is genuinely compelling.

### Marlowe / Fenra Veil — Split Crown Operative
**Appearance:** Early fifties, a former court secretary's posture — straight-backed, precise in her movements. She passes as an inn regular who does light accounting work for local merchants.

**Manner:** Professionally warm. She has the social adaptability of someone who spent fifteen years managing courtiers.

**Wants:** Maintain her position and the double-income arrangement she's built. She does not want Cabal attention on any of her side work.

**Secret:** She is feeding intelligence to Caedric's network as well as the Cabal. If this becomes known to either organization, she is dead. This makes her blackmailable — carefully.

---

## RUNNING IT

### Scene 1: Learning About Cross

> The headman of Thornfield is apologizing. He has been apologizing, in an indirect and increasingly embarrassed way, since you arrived — something about a conversation three days ago, a merchant who was listening, a report he's afraid was sent somewhere it shouldn't have been. "I wasn't thinking," he says. "I talk when I'm worried. I should have known better."

Osric Thale provides a description of Cross and mentions he's still in the area — his pattern is to work this road for a week at a time before cycling back to Vaelthorn. He's probably in Millhaven or Greyford. He's easy to find if you know what he looks like.

### Scene 2: Finding Cross

Cross is in Greyford, two hours south, making legitimate calls on spice buyers. He is exactly as unremarkable in person as described. If the players approach as travelers with no apparent foreknowledge:

| DC | Skill | Outcome |
|----|-------|---------|
| 13 | Insight | Something about his manner is calibrated; he's performing approachability rather than feeling it |
| 15 | Perception | He made a specific visual check of exits when the players entered |
| 11 | Persuasion (casual conversation, testing reaction to Thornwick) | He shows interest that's a fraction too controlled |

If players confront him directly with evidence or demonstrate they know his actual role, he goes still. He doesn't panic — he's professional enough not to panic. He asks what they want.

### Scene 3: The Information Play

**Option A — Turn Cross:** Offer him protection and continued income in exchange for carrying disinformation up the chain. This requires agreeing on a cover story for Thornwick's bell (the headman's records show it failed mechanically, regular maintenance issue, nothing unusual), forging or obtaining supporting documentation, and trusting Cross to deliver it accurately. He will, if the payment is real and the story is plausible.

**Option B — Marlowe:** Using Cross's undelivered message as leverage, approach Marlowe at the Split Crown. She will negotiate. She does not want the Cabal knowing about her side arrangement. If she agrees to transmit disinformation about Thornwick, the Cabal focus shifts. Blackwell's section, receiving a similar story from Marlowe, files the Thornwick report as low-priority.

**Option C — Captain Durn:** Find and approach Durn directly with a plausible story. This is the most dangerous option — Durn is professional and suspicious — but a direct source telling Blackwell's intelligence section that Thornwick's bell silence is mundane carries weight if the cover story is airtight.

| DC | Skill | Outcome |
|----|-------|---------|
| 12 | Deception (plausible cover story to Cross) | He's interested; asks two follow-up questions |
| 15 | Deception (cover story survives his professional scrutiny) | He agrees to carry the disinformation; asks for payment terms |
| 14 | Persuasion (negotiating with Marlowe via leverage) | She agrees, visibly reluctant, to pass the cover story onward |
| 17 | Deception (selling Durn on the mundane explanation) | Durn files it as resolved; Blackwell's focus shifts elsewhere |

**Resolution Path A — Turned Asset:** Cross becomes a recurring contact. He can carry information in both directions, selectively and carefully. The disinformation about Thornwick buys two to four weeks before someone revisits the question.

**Resolution Path B — Neutralized Network:** If Marlowe is also turned or neutralized, the entire Chain from Thornfield to the Cabal goes quiet. Blackwell's section has a summary with no follow-up confirmation. The gap gets noticed eventually, but not immediately.

---

## IF IT FAILS

If the players cannot intercept Cross, turn him, or plant disinformation within a week of arrival, the Cabal sends a three-person assessment team to Thornwick. Blackwell's intelligence section assigns a permanent watcher to the Thornwick road. The situation at Thornwick becomes significantly harder to manage quietly.

The players may find this a useful crisis later — a Cabal assessment team in Thornwick is also a concentrated Cabal target — but it forecloses the quiet option.

---

## REWARDS

- No standard financial reward; this is intelligence work
- Cross as a turned asset: access to Cabal message traffic on the Vaelthorn-Dunwater road
- Marlowe's undelivered letter (if obtained): intelligence about Caedric's network that neither Caedric nor the Cabal knows has been intercepted
- Reduced heat on Thornwick: the bell-silence investigation goes cold, allowing other quests in the Thornwick-area thread to proceed without Blackwell observation

---

## CONNECTS TO

- **Unclaimed Crates at Dunwater** — the same intelligence chain runs through Marlowe; Cross and the Dunwater crates are part of the same Cabal logistics network
- **Bell at Thornwick** — directly; this quest is the consequence of that quest's discovery becoming known
- **Harwick Holds the Line** — Blackwell's intelligence section is the same organization watching both Thornwick and Harwick; success here reduces their operational bandwidth
