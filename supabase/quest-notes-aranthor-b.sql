# The Revenue Officer's Convenient Fever
### *Nobody pushed him. This is the important thing. Nobody pushed him.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Cobbsgate, a fishing village on the River Ael's eastern branch |
| **Level Range** | 2–4 |
| **Duration** | 60–90 minutes (single session) |
| **Quest Giver** | Ambiguous — no formal giver; situation presents itself |
| **Quest Type** | Town — Crisis Management, Cover-up, Moral Texture |

---

## THE SITUATION

A Blackwell revenue officer named Rendal Hess arrived in Cobbsgate four days ago to assess the village's fishing output for the new quarterly taxation. On his second morning, he was walking the harbor pier and his boot caught on something, and he went into the river. He was rescued — by the same fisherman who, it turns out, had loosened the pier plank the previous evening.

Hess is alive. He is currently bedridden at the harbormaster's house with a genuine fever from the cold water and the shock, being nursed by the harbormaster's wife, who is conscientious about this and slightly resentful about it. He will recover in three to five days.

The fisherman who loosened the plank is a man named Corrat Hindle. He is not a criminal type. He is fifty-two years old, has fished this stretch of river for thirty years, and did something panicked and desperate because Hess's tax assessment, if submitted as estimated, will take thirty percent of Cobbsgate's annual revenue. He is now sitting on this secret with the specific quality of a man who made a very bad decision and cannot find a way to unmake it.

Hess, in his fever bed, has begun to suspect the fall was not accidental. He is writing a report.

---

## BACKGROUND

*(DM-only — do not share with players)*

The pier plank was loosened, not removed — Corrat thinned two of the securing bolts just enough that a man's weight at the wrong angle would give way. He did not intend for Hess to fall into the river; he intended for Hess to stumble, hurt his ankle, and need to be transported back to Dunmore for medical attention before completing the assessment. He was not trying to kill anyone. The fact that Hess nearly drowned is the thing Corrat cannot live with.

Hess is a Blackwell contractor — not a soldier, not an ideological loyalist, just a man who was hired for this work because he is meticulous and difficult to bribe. He is good at his job. He keeps careful records. His current notes, if submitted, will accurately reflect Cobbsgate's output.

