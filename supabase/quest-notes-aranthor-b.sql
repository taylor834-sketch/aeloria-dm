# The Unclaimed Crates at Dunwater
### *The barge arrived three weeks ago. The captain left. The crates are still here.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Dunwater, a barge-yard town on the River Ael |
| **Level Range** | 3–5 |
| **Duration** | 60–90 minutes (single session, potentially 2) |
| **Quest Giver** | Torvald Finch, barge-yard master |
| **Quest Type** | Investigation — Shadow Cabal, Sensitive Handling |

---

## THE SITUATION

A flat-bottomed cargo barge called the *Kestrel's Draw* arrived at Dunwater's barge-yard fifteen days off its registered schedule with a crew reduced to two — a river pilot and a deckhand who spoke to no one and left the next morning. The captain, a man named Borrec, paid for two months of storage on seven sealed wooden crates, told Torvald Finch the crates contained "medicinal compounds in transit," and left on horseback by midday.

Fifteen days later, the crates are unclaimed. Nobody has sent a courier. Nobody has written. Torvald, who has been in the barge business for twenty-two years, has never had a shipment this persistently uncollected. He has been waiting long enough that it now feels like waiting for permission. He has not opened the crates. He is not sure he wants to know what's in them. He wants this to be someone else's problem.

The players are the first people who have arrived at Dunwater in the right combination of apparent authority and willingness to listen.

---

## BACKGROUND

*(DM-only — do not share with players)*

The crates contain Shadow Cabal ritual materials — components for a marsh-anchoring ritual intended for the Thornmere cell, which is establishing a new working site in the marshland south of Dunwater. The specific contents: nested iron rings for the outer binding circle, a bundled set of ritual instruments wrapped in oilcloth, a sealed letter in cipher addressed to the Thornmere cell leader, and a paper manifest listing four other drop points across Aranthor.

Captain Borrec did not deliver the crates because he was arrested by Blackwell's customs inspectors at the first toll-station south of Dunwater on an unrelated charge — prior smuggling, wool tariff evasion. He's currently being held in the Dunmore garrison's holding cells. He has not told Blackwell's people what the crates contain because he doesn't know and because talking about his clients is not his business practice.

The critical complication: the Cabal's crates carry detection seals — thin lines of silver-black pigment worked into the lid of each crate, invisible to normal inspection, readable by anyone with DC 14 Arcana. If a seal is broken — that is, if a crate is opened — a delayed alert reaches the Thornmere cell within 48 hours. Not immediately dangerous, but the cell will send someone to investigate why their shipment has been disturbed.

The manifest, if retrieved intact without breaking seals, is a significant intelligence find: it names four Aranthor drop points by location code and provides the Thornmere cell's receiving protocol. The cipher on the letter is breakable (DC 17 with appropriate tools, or DC 12 with access to a Sentinel Order cipher guide). The letter itself confirms Thornmere as a new cell site and names the cell leader by operational title — "the Gardener."

---

## KEY NPCs

### Torvald Finch — Barge-yard Master
**Appearance:** Late fifties, heavyset, with the specific weathering of a man who has spent decades on the waterfront. Ink-stained fingers from his ledgers.

**Manner:** Unhurried and factual. He does not perform distress; he has a problem and he is describing it accurately and waiting for a solution.

**Wants:** For the crates to be someone else's responsibility. Not because he suspects anything criminal — he just doesn't want the liability, the storage dispute, and the awkwardness of an uncollected shipment.

**Secret:** He has been keeping a second logbook of irregular arrivals at Dunwater for the last three years, mostly out of professional habit. If the players ask about other unusual shipments, he'll find the relevant pages.

### Borrec — The Captain (not present; in Dunmore custody)
**Appearance:** Forties, dark, compact, with a professional smuggler's habit of making direct eye contact.

**Manner:** Cooperative on the specific subject of his own innocence; profoundly uncooperative on the subject of his clients and cargo.

**Wants:** To get out of Dunmore's holding cells. He will trade information about the crates in exchange for assistance with his release — but he has to be found first.

**Secret:** He's done Cabal transport work four times. He knows the crates are not medicinal compounds. He has been paid well not to think about what they are.

---

## RUNNING IT

### Scene 1: The Barge-yard

> Dunwater's barge-yard smells of river water and old rope. Three barges are docked and two are being loaded. The crates in question are under a canvas lean-to near the eastern storage shed — seven of them, stacked in a specific pattern, sealed with iron clasps. They look ordinary. They are the right size for crates of medicinal supplies. There is nothing about them that is obviously wrong. Nothing, except that they have been sitting here for fifteen days and nobody has come for them.

Torvald meets the players here. He gives them his account, shows them the storage receipt in Borrec's name, and says: "I've sent two letters to the return address. No response. I'm not going to sit on these indefinitely. What do you want me to do?"

### Scene 2: Examining the Crates

If players examine the crates carefully before opening anything:

| DC | Skill | Outcome |
|----|-------|---------|
| 11 | Investigation | The crates were made by a skilled carpenter; not a commercial supplier — custom work |
| 14 | Arcana | The detection seals become visible — fine lines in a pattern that reads as a notification ward |
| 16 | Arcana | The ward will alert its anchor point if the seal is broken; anchor estimated at 40–60 miles south |
| 12 | Perception | The manifest is slid under the outermost crate, not inside — it fell, or was placed, outside the sealed area |

The manifest is accessible without breaking any seal. The letter and the ritual components require opening a crate, which triggers the 48-hour alert.

**The key decision:** Players can gain full intelligence by breaking seals, but they have 48 hours before someone arrives to investigate. They can gain partial intelligence (manifest plus outside evidence) without triggering anything. They can also leave the crates undisturbed, track Borrec in Dunmore, and get the full picture from him — a longer play but one that leaves the Cabal unaware.

### Scene 3: The Investigation Branches

**Branch A — Work from the manifest:** The location codes on the manifest can be cross-referenced against River Ael shipping records (available from Dunwater's harbormaster) to identify probable pickup sites. This requires a day of investigation but doesn't trigger the Cabal.

**Branch B — Open the crates:** Full intelligence on the ritual materials and a clear read on the Thornmere operation. The 48-hour clock starts. The players must either intercept the investigating Cabal operative or relocate before arrival.

**Branch C — Find Borrec:** He's in Dunmore. Getting him out requires either legal maneuvering, a bribe to the right garrison official (50 gp, DC 14 Persuasion), or a more direct approach. Borrec, once freed, will describe the Cabal contact who hired him — a woman, middling height, forgettable face, who met him three times at the Dunmore docks.

**Resolution Path A — Intelligence Extraction:** Players leave the crates in place but pocket the manifest and break only one seal (the letter) in a controlled way. They move location. They have 48 hours and substantial intelligence.

**Resolution Path B — Interdiction:** Players bring Sentinel Order contacts into the situation or use the intelligence to locate the Thornmere cell before it receives the materials. The crates become bait.

---

## IF IT FAILS

If players simply leave after hearing Torvald's account without acting, the Cabal sends a courier to collect the crates within the week. The operation at Thornmere proceeds. The players may encounter the results later — a marsh-anchoring site that has been active for weeks is significantly more dangerous to disrupt than an incipient one.

If players open all the crates and then don't act on the 48-hour window, the Cabal sends a three-person team to investigate. They find a disturbed storage site and move the remaining materials. The manifest is gone. The Cabal knows someone is in the intelligence loop and increases operational security.

---

## REWARDS

- No financial reward from Torvald; he's relieved, not grateful in a monetary sense
- The manifest: four Cabal drop points across Aranthor, each a potential quest thread
- The cipher letter (if decoded): confirmation of the Thornmere cell and the identity of the Gardener
- Borrec, if freed: an ongoing contact with knowledge of Cabal transport routes; not reliable but not useless
- Sentinel Order gratitude: if the players bring this to Commander Elyndra, it substantially advances their picture of the Cabal's Aranthor network

---

## CONNECTS TO

- **Notice at Thornfield** — the Thornmere operation and the Thornfield intelligence leak are related; both feed into the same cell structure
- **Bell at Thornwick** — ritual materials from these crates are the same category as the warding-bell suppression tools; same Cabal campaign, different phase
- **The Cracked Crown** — the Cabal's Aranthor operations are coordinated with the succession crisis; the Thornmere cell's timing is not coincidental
