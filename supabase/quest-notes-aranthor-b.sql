## THE SITUATION

Maevan Shore has told her account to three different people and been dismissed each time. She is not a fisherman, not a harbormaster, not anyone with official standing in Saltmire — she is a woman in her forties who mends nets and gathers saltgrass and pays attention. She has seen lights in the deep bay water three times in two months. The first time: a pale blue-green shimmer in the deepest channel, midnight, lasting perhaps ten minutes. The second: a slow circling pattern that mapped a rough shape she estimated at forty feet. The third, last week: something that rose briefly toward the surface and she saw, against the moonlight, a silhouette too large to be any fish she could name.

The Lighthaven harbormaster, Dorvil Cray, dismissed her twice. He stopped dismissing her three days ago when two of his boats returned with parallel scoring along their keels — smooth, continuous grooves running for six feet along the underside of the hull, spaced at an interval that matched the width of a large, flat form passing beneath.

Cray is now quietly alarmed. He has not made this information public because he cannot afford a panic in a harbor town. He is handling it by adding extra lookouts, closing the deep-water channel at night, and hoping whatever it is goes away.

It will not go away.

---

## BACKGROUND

*(DM-only — do not share with players)*

Three months ago, during maintenance dredging in the deep bay channel, a sand-barge crew disturbed something they described only as "a layer we weren't expecting — wrong color, wrong feel, pulled tools down." The barge crew refused to continue dredging that section and was replaced. The replacement crew returned two days later with the barge's anchor chain scored in a pattern that matched the hull markings. The dredging was quietly abandoned.

What is in the channel: a 200-foot section of the bay floor that has been physically altered — scored, as if by tremendous pressure from below, in a pattern the Ashcroft Mariners (Saltmire's fishing guild, three generations old) recognize from their founding stories. Their founders settled this bay because of what rests in its depths — a Pre-Founding entity they called the Warden of the Old Shore, bound during the First Sealing alongside the other entities that predated human settlement of Aeloria.

The entity is not awake. It is becoming aware. The distinction matters: it is not attacking, it is not consuming, it is not malevolent in any operational sense. It is an ancient bound thing whose binding is eroding as the Shadow Lords' suppression of the warding bells weakens the broader network of First Sealing anchors. The lights are its dreaming. The keel markings are where it turned over in that dreaming, brushing the surface of the world it can sense but not yet reach.

The Ashcroft Mariners know. They closed ranks four months ago when the senior member of the guild felt the channel's bottom during a dive and reported what he found to the guild council. They have added extra lookouts, quietly. They have retrieved a cache of Founding-era documents the guild has held in trust for three generations, never understanding what they were for. The senior Mariner has begun reading them with the specific focus of a man who has just realized they were instructions.

**The risk:** If the entity achieves full waking — which requires the Bay Anchor (a First Sealing component sunk in the deepest part of the channel) to be removed or destroyed — it will not be immediately hostile to humans. But it is vast, and its movement through the bay will end Saltmire's fishing economy. And the process of its full waking will call attention — Shadow Lord attention — to the bay.

**What can be done:** The binding can be reinforced by replacing the Bay Anchor with a recharged version. The Sentinel Order has the knowledge for this and, if given the Ashcroft Mariners' founding documents, can determine the ritual. Alternatively, a temporary suppression — reestablishing one of the larger warding bells in Saltmire, which has been silent since the Cabal's campaign — can slow the erosion long enough for a proper repair.

