# The Sealed Strongbox at Warden's Ford
### *Every faction wants the money. The man holding it just wants to know who's in charge.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Warden's Ford toll crossing |
| **Level Range** | 1–5 |
| **Duration** | 45–90 minutes |
| **Quest Giver** | Cass Henner, Royal Reeve |
| **Quest Type** | Town — Faction Pressure, Moral Choice |

---

## THE SITUATION

Royal Reeve Cass Henner has been collecting tolls at Warden's Ford for thirty-one years without missing a day. Every quarter, he remits collected revenue to the Crown treasury via bonded courier. He has not missed a remittance in his career. This quarter, he has 1,200 gold pieces in a locked strongbox and nowhere to send it. The Crown treasury is, functionally, nonexistent — its clerks have scattered across the three succession claimants' administrations, and no bonded courier will accept a remittance without a countersigning treasury authority. There is no countersigning authority anyone agrees on.

Henner will not give the money to the wrong party. He understands that his personal opinion of who governs Aeloria is irrelevant to the legal question of who holds the Crown, and he has been waiting sixty-three days for someone to present him with a convincing legal answer. Three factions have sent representatives. None of them has satisfied him. He's been sleeping in the toll-house with the strongbox chained to his cot.

---

## BACKGROUND

*(DM-only — do not share with players)*

No faction has a technically clean claim to these funds. Garron Blackwell controls the military apparatus but has not been crowned and holds no treasury appointment. Caedric Valemont has Shadow Cabal backing and some legal documents whose authenticity Henner correctly suspects. Elowen Valemont is the rightful heir but holds no public institutional authority — she is in hiding, and her Crown Loyalist intermediaries cannot produce a writ that would satisfy a careful man without revealing her location.

Henner's standard is high but not impossible. He will accept: a writ bearing a recognizable royal seal from someone he can verify as holding legitimate appointment authority; a compelling legal argument from someone with obvious institutional standing (a senior Sentinel Order officer, a credentialed Crown court functionary); or physical evidence of Crown Loyalist institutional structure — a chain of title showing command authority flowing from Elowen downward. He cannot be bribed. He cannot be threatened into compliance (he will simply escalate to his deputy and lock himself in). He can be argued with, and he will change his mind if the argument is sound.

His deputy, Alec, is a quiet man who keeps meticulous records. If the money is taken by force or deception, Alec records the perpetrators, their descriptions, their stated affiliation, and the date. That record will surface in a later session at exactly the wrong moment. Faction representatives from all three claimants are, at any given time, within a day's ride of Warden's Ford. The players are being watched.

---

## KEY NPCs

### Cass Henner

**Appearance:** Late fifties, compact and precise, dressed in toll-officer livery that has been brushed and maintained despite clearly being old. He has the face of a man who has never found an ambiguity he could not resolve by looking harder at the relevant documents.

**Manner:** Polite, unhurried, fundamentally reasonable. He asks clarifying questions. He explains his reasoning. He does not raise his voice. He is not impressed by rank, urgency, or the confidence of the person speaking.

**Wants:** A legally defensible answer to a legal question. He wants to do his job correctly, the way he has always done it. He does not want the money, he does not want power, and he does not want to be in the middle of a succession crisis. He wants someone to tell him, with evidence, who the rightful government is.

**Secret:** He already has a private opinion — he believes Elowen Valemont is the rightful heir based on his reading of succession law. He will not act on this opinion without documentation. But if the Crown Loyalists present a clean legal case, he will find it more persuasive than he lets on.

---

## RUNNING IT

> *The toll-house at Warden's Ford is a stone building that smells of river damp and old ledger paper. A man sits behind a writing desk that is completely clear except for one ledger, one inkwell, and one quill. He stands when you enter — not in deference, but because he takes visitors seriously enough to stand — and says: "I'm Reeve Henner. I expect you're here about the strongbox. I'll explain my situation plainly, and then I'll ask you a question I've been asking for sixty-three days. Nobody has answered it yet to my satisfaction."*

**Beat 1 — The Explanation.** Henner lays out the problem without drama: 1,200gp in collected tolls, no functional treasury to receive it, three claimants all presenting dubious authority, and a deputy who is watching all of this very carefully. He is not asking for help. He is asking a legal question: who is the Crown? The players can ask follow-up questions. He will answer them honestly, including confirming that he finds Caedric Valemont's documents suspicious and that Blackwell's representative had no treasury appointment documents at all.

**Beat 2 — The Case.** Players must make their argument. This is primarily a roleplay beat, but the skill table below governs how much ground they gain. The key insight: Henner does not need to be *convinced* of Elowen's claim — he needs to be presented with institutional evidence he can record in his ledger. A compelling personal argument buys goodwill. Documentary evidence buys compliance. Force buys an entry in Alec's records.

**Beat 3 — The Outcome.** If satisfied, Henner transfers the strongbox to the players' custody with a signed receipt (he keeps a copy). If unsatisfied, he offers to hold the money thirty more days while the players locate proper documentation. If threatened or coerced, he complies under visible protest while Alec writes.

| DC | Skill | Outcome |
|----|-------|---------|
| 10 | Persuasion | Henner extends good faith; 30 more days, no force |
| 14 | History or Insight | Identifies the weakest point in Caedric's documents; Henner confirms suspicion aloud |
| 16 | Persuasion (with Loyalist credentials) | Henner accepts institutional authority argument; transfers funds |
| 18 | Investigation | Finds a procedural technicality that lets Henner remit to a Crown Loyalist holding account without requiring a full treasury receipt |
| 20 | Persuasion (with royal writ) | Full compliance, signed receipt, Henner requests ongoing correspondence |

**Resolution Path A — Legal Argument.** Players make a sufficiently strong case for Crown Loyalist institutional authority. Henner accepts, transfers the strongbox, and issues receipts in triplicate. He requests that if Elowen Valemont's people have an active command structure, someone send him a letter confirming the chain of authority — he's been wanting one.

**Resolution Path B — Deferred with Documentation.** Players cannot produce the right documents today but promise to return within thirty days. Henner agrees to hold. This is a live quest hook that can be resolved when the party reaches a Crown Loyalist officer with the authority to issue a writ. The strongbox becomes a recurring element.

**Resolution Path C — Force.** Players take the money by intimidation or deception. Alec records everything. The party gains 1,200gp and a future complication: a detailed description of them and their stated affiliations in an official toll-house ledger that will be subpoenaed by at least one faction within two sessions.

---

## IF IT FAILS

**Outcome A — Cabal Takes It.** If the players fail to establish Loyalist authority and the Cabal's agent returns before the thirty-day window closes, Caedric's representative produces a convincingly forged treasury appointment and Henner, satisfied by the legal form even if not the content, remits the funds. The Shadow Cabal has 1,200gp for operational expenses in the coastal district. Future supply-chain hooks for Loyalist operations become more difficult.

**Outcome B — Blackwell Seizure.** A Blackwell officer arrives with a military detachment and exercises what he calls "wartime treasury emergency authority." Henner protests formally. Alec writes a very long entry. The money goes to Blackwell's garrison payroll. Henner is formally aggrieved and will cooperate with any future legal challenge — he is an excellent witness.

---

## REWARDS

- **Full resolution (Loyalist custody):** 1,200gp transferred plus Henner's written receipt (functions as a minor institutional credential with Crown Loyalist NPCs)
- **Deferred with return:** Henner's goodwill and a standing contact at Warden's Ford (future information source)
- **Force:** 1,200gp, future complication, no institutional benefit

---

## CONNECTS TO

- **The Cabal Offer at Kettermere** — the 1,200gp would accelerate Cabal operations in the coastal district if lost
- **The Mill Crack and the Loyalist Engineer** — Vorel Dass can provide institutional documentation if the party knows to ask him
- Any Crown Loyalist quest line involving establishing a command chain for Elowen Valemont's claimant network
