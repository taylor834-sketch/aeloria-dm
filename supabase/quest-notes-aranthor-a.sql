## THE SITUATION

Three Blackwell loggers went into the northern Greywood two weeks ago on a survey assignment and did not return. Blackwell doubled armed escorts on all subsequent logging parties but sent no search party into the northern section — the garrison commander made a quiet decision that three surveyors are not worth the exposure of a full search in territory that is already making his escorts uneasy. The families of the missing men are not receiving information. Blackwell's official position is that the investigation is ongoing.

Independent operator Coll Sarne has a thirty-year lumber charter that predates Blackwell's current interest in the Greywood. He has been watching Blackwell's operation with the attentive hostility of a man whose legal territory is being encroached on. He knows the northern Greywood better than any Blackwell surveyor, and he knows that the deer have been moving south out of the northern sections for three weeks — not seasonal movement, not predator pressure, something else. He has not gone north himself because his operation is in the southern Greywood and his charter does not obligate him to.

