## BACKGROUND

*(DM-only — do not share with players)*

The three missing Blackwell surveyors found something in the northern Greywood. They are not dead — they are held in place by it, a phenomenon that is best described as roots growing around intent: they can move, they can eat, they are physically unharmed, but they cannot leave the northern section. They have been trying for two weeks and cannot explain why their feet turn back. A DC 16 Arcana check on the northern section's boundary identifies the phenomenon as old Forest warding magic — not malicious, but containment-oriented, responding to something the surveyors disturbed.

The northern Greywood is expanding south at approximately forty yards per month. This has been happening for six months. It is not Cabal activity — it predates the current succession crisis by decades. Something in the deep northern section was sleeping and is waking. The deer know it. The birds know it. The northernmost of Coll Sarne's cutting crews have been uneasy for two months and can't say why.

Coll Sarne has been rerouting drainage channels that Blackwell's mill requires to season green timber properly. His method is technically legal — the channels cross his charter land, and he has full authority to manage water flow on his property. He has been doing this deliberately for four months. The effect: Blackwell's mill is processing timber that retains moisture it should not, and Fort Ashveil is being built with lumber that will begin showing structural failure in three to five years. Nobody has checked. The fort's commander has no reason to look at the seasoning records.

Sarne will not volunteer his sabotage. He considers it a private act of legal warfare within the rules he has agreed to operate under. If players surface it through Investigation, he will confirm it without shame and explain his reasoning at length: Blackwell is operating on his charter land, the drainage rerouting is within his legal rights, and if Blackwell had followed charter law they would not have a problem.

