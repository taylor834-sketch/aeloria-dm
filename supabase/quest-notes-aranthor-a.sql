# The Mill Crack and the Loyalist Engineer
### *The crack is real, the repair is urgent, and the engineer has two jobs. Both of them matter.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Millbrook, limestone shelf district |
| **Level Range** | 1–5 |
| **Duration** | 60–90 minutes |
| **Quest Giver** | Vorel Dass, civil engineer (Crown Loyalist intermediary) |
| **Quest Type** | Town — Faction Intrigue, Practical Repair |

---

## THE SITUATION

The limestone shelf that powers Millbrook's three mills has developed a crack measuring fourteen feet across its load-bearing face. Left unaddressed, the shelf will fail catastrophically within two seasons, taking the mills and the upstream retention wall with it. The two miller families — Oske and Corn, feuding for eleven years over water rights and a disputed inheritance — could not agree on a single engineer or a cost-sharing arrangement until the crack became large enough to threaten both operations simultaneously. They hired Vorel Dass jointly, each family believing they got the better deal on the fee split.

Vorel is competent, even-handed in his engineering work, and using this contract as cover for a Crown Loyalist mission: establishing a supply routing contact in Millbrook for the overland network that moves resources toward Elowen Valemont's position. He needs one reliable local who can move sealed correspondence and occasional goods through Millbrook without drawing Blackwell attention. He has been in town for three weeks, done excellent preliminary work on the crack, and has not yet found his contact.

---

## BACKGROUND

*(DM-only — do not share with players)*

The crack is structurally urgent and completely genuine. Vorel is not fabricating the crisis for cover — the timing was fortunate, and he took the contract because it was real work that justified real presence. He estimates the shelf requires iron pinning and mortar consolidation, a three-week project with a crew of four. The Oske-Corn feud will complicate hiring because each family has pressured local workers to declare loyalty — Vorel has been managing this diplomatically and is frustrated by it.

A Blackwell informant named Seel, who passes as a seasonal grain buyer, has noticed that Vorel asks unusual questions for an engineer — his conversations tend toward who owns what, who moves goods through town, and who has standing with both families despite the feud. Seel has not yet reported this; he wants confirmation before committing to a report because the last false lead he filed cost him credibility. Players who make contact with Vorel will become part of what Seel is watching.

The Oske-Corn feud can be actively resolved, deliberately inflamed, or simply worked around. Resolution reduces local friction and makes Vorel's contact search faster. Inflaming it creates cover — a town in open conflict is less likely to notice a Loyalist network establishing itself. Working around it is Vorel's current approach, and it's slow.

Vorel's contact candidate: Petra Oske, daughter of the elder Oske miller, who has quietly maintained civil relations with the Corn family through a friendship with their apprentice miller. She moves freely through both properties. She is not yet aware she's being evaluated.

---

## KEY NPCs

### Vorel Dass

**Appearance:** Late thirties, medium height, always has chalk on his hands and at least one set of measurement drawings rolled under his arm. Dresses practically. His eyes evaluate structural load-bearing potential in everything he looks at, including people.

**Manner:** Even-handed, technically precise, socially calibrated. He gives both families exactly equal professional deference in public, which is a practiced performance he finds mildly exhausting. In private conversation he is more direct and more tired.

**Wants:** To complete the repair correctly and establish his Loyalist contact without those two missions interfering with each other. He is aware they could. He would prefer outside help identifying and vetting Petra Oske so he doesn't have to do both himself.

**Secret:** Crown Loyalist intermediary building a supply routing contact in Millbrook. He will not reveal this without a clear Loyalist credential from the players or an extremely well-calibrated approach. He'll neither confirm nor deny under pressure — he'll change the subject to the crack.

---

### Petra Oske

**Appearance:** Mid-twenties, second daughter, has her father's jaw and her mother's patience. Works in the Oske mill's office doing accounts — her handwriting is noticeably precise.

**Manner:** Careful and friendly without being open. She has learned to be diplomatic in a household where mentioning the Corns at the wrong moment ends suppers. She is tired of the feud and has never said so to her family.

**Wants:** For the feud to end, which she has accepted is not something she can make happen, and for her own life to extend beyond its current radius, which she suspects might be possible but hasn't been given a reason to believe yet.

**Secret:** She has been maintaining a quiet friendship with the Corn family's apprentice miller for two years, meeting at the market on Seventhday. Neither family knows. This doubles as her evaluation metric from Vorel's perspective — she has already demonstrated the discretion and cross-faction movement he needs.

---

## RUNNING IT

> *Millbrook's three mills run off a single limestone shelf that juts twelve feet over the river — you can hear all three sets of millstones from the bridge. A man in working clothes is crouched at the base of the shelf with a measuring rod and a lamp, marking chalk lines along a crack that runs the full length of the load-bearing face. He doesn't look up when you approach, but he says: "Fourteen feet, eight inches, and it's moved three inches since I marked it last week. Either of you know anything about limestone consolidation?" He still hasn't looked up.*

**Beat 1 — The Engineering Problem.** Vorel is happy to talk about the crack. This is genuine enthusiasm. The repair is interesting, the constraint is interesting, the feud-driven labor problem is annoying but technically solvable. Players who engage with the engineering problem build rapport quickly. Players who try to steer toward politics or faction work are gently redirected — Vorel does one conversation at a time.

**Beat 2 — The Feud.** The Oske-Corn history emerges through NPC interactions. The elder Oske miller, Bram Oske, will pull players aside to explain that the Corns have been deliberately undermining his family for eleven years. Elder Corn miller Wyla Corn will pull players aside to explain that the Oskes have been deliberately undermining her family for eleven years. Both accounts are internally consistent. A DC 14 History or Insight check reveals that the original dispute was a genuine legal ambiguity in a water rights charter that neither family caused, and both have been nursing it into something larger ever since.

**Beat 3 — Seel's Attention.** A player with a passive Perception of 13 or higher notices that the grain buyer who's been around the market for two weeks has been in earshot of three separate Vorel conversations. Players who investigate Seel (DC 12 Investigation) will find his buying ledger entries are unusually thin for the amount of time he spends in town. He is a Blackwell informant waiting for confirmation. If pressed, he deflects. If cornered with evidence, he'll make a report regardless to cut his exposure.

| DC | Skill | Outcome |
|----|-------|---------|
| 10 | Persuasion | Vorel confirms the crack timeline; discusses the labor problem openly |
| 13 | Insight (Vorel) | Recognizes his questions go beyond engineering; he's evaluating people |
| 15 | Investigation (Seel) | Confirms Seel is an informant; determines his reporting timeline |
| 16 | Persuasion (with Loyalist credential) | Vorel confirms his mission; requests help vetting Petra Oske |
| 18 | Persuasion (Oske-Corn reconciliation) | Both families agree to a formal water rights arbitration; feud de-escalates |

**Resolution Path A — Contact Established.** Players help Vorel vet and approach Petra Oske. She accepts the contact role. The repair proceeds. Seel's report is delayed or discredited. The Loyalist network has a functioning Millbrook node.

**Resolution Path B — Feud Resolved.** Players broker a formal arbitration. Both families agree on a charter amendment. The repair proceeds efficiently. Vorel's cover is strengthened by the chaos of resolution having passed. Seel loses his monitoring pretext.

**Resolution Path C — Seel Neutralized.** Players expose Seel as an informant to the community (he's operating without local authority). He's asked to leave. Vorel's mission continues with reduced surveillance. This creates a small debt with the Blackwell faction if it surfaces who was responsible.

---

## IF IT FAILS

**Outcome A — Seel Files His Report.** Vorel's unusual behavior is reported to the nearest Blackwell garrison. An officer arrives within a week to question him. Vorel's cover survives inspection (his engineering work is real), but his contact search is suspended. The Loyalist network loses its Millbrook node opportunity for this season.

**Outcome B — Crack Ignored.** If players focus entirely on the faction intrigue and nobody addresses the repair timeline, the shelf fails in the second month after the session. Both mills are destroyed. Millbrook's economy is disrupted. The Oske-Corn feud becomes irrelevant. The limestone collapse also damages the river road, which is a key supply route.

---

## REWARDS

- 60gp from each miller family for facilitating the repair agreement (120gp total)
- Vorel Dass as a standing Crown Loyalist contact with engineering expertise
- Loyalist commendation if contact network is established
- Petra Oske as a Millbrook information source if her contact role is cleanly arranged

---

## CONNECTS TO

- **The Sealed Strongbox at Warden's Ford** — Vorel can provide institutional documentation if players know to ask
- **What the Thornkeeper Saw** — the Loyalist supply network Vorel is building connects to coastal district operations
- Blackwell faction arc: Seel's report, if filed, establishes party presence on Blackwell's watch list
