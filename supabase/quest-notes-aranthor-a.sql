# The Cabal Offer at Kettermere
### *She refused them twice. The silence after the third non-visit is louder than either refusal.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Kettermere salt-works, coastal district |
| **Level Range** | 1–5 |
| **Duration** | 60–90 minutes |
| **Quest Giver** | Emm Crane, salt-works operator |
| **Quest Type** | Town — Faction Threat, Family Stakes |

---

## THE SITUATION

Emm Crane has operated the Kettermere salt-works under her family's charter for twenty-two years. She received a Shadow Cabal purchase offer eight months ago — courteous, well-funded, vague about the buyer's identity. She refused. A second offer came four months later, higher, more insistent. She refused again. The Cabal's agent has not returned for a third attempt, and it has been three months since the second refusal. Emm has been sleeping in two-hour intervals since the silence began.

She is not given to asking for help. She has done the math on what the silence likely means, and the math isn't good. A persistent buyer who stops making offers has either found another approach or is preparing one. She has been watching the salt-works entrance at night. She has sent her youngest daughter to stay with a cousin in Aranthor proper. She has not told anyone why she's doing these things, because saying it aloud would require her to believe it herself.

---

## BACKGROUND

*(DM-only — do not share with players)*

The salt-works sits atop a ley-line convergence point — one of seven nodes the Shadow Cabal has identified as suitable for maintaining a ritual presence in the coastal district. The convergence does not require the salt-works to be destroyed or altered; it requires the Cabal to have uncontested access to the underground chamber beneath the works' eastern boiling house. Emm's predecessor sealed that chamber forty years ago because it made the workers uneasy. She has never opened it and does not know what it contains.

The Cabal's agent, a man who calls himself Davan Lore, has not escalated to violence because the Cabal prefers not to create local investigations. His next approach will not be an offer. It will be Pell — Emm's son, nineteen years old, apprenticed to a traveling artificer and currently in Aranthor. Pell has been corresponding with Lore for two months about magical theory and possible training opportunities. He does not know who Lore represents. Lore intends to offer Pell a scholarship to a real magical institution as a lever — once Pell is enrolled and dependent, the Cabal will explain to Emm what they want and what Pell's continued educational opportunity depends on.

The Mereford Inn's proprietor, Holt Cass, can confirm that a man matching Lore's description has asked about the salt-works on at least two occasions. A DC 13 Investigation check of the salt-works' eastern boiling house reveals the sealed chamber and, below its sealed door, a faint resonance that a DC 16 Arcana check identifies as old ritual preparation.

**Resolution options:** Expose Pell's correspondence with Lore before the Cabal executes the leverage; locate and remove Lore from the coastal district; or help Emm establish formal faction protection (Crown Loyalists or Sentinel Order) that makes Cabal acquisition politically costly.

---

## KEY NPCs

### Emm Crane

**Appearance:** Mid-forties, fourth-generation Kettermere native, carries herself like someone who has been lifting things her whole life. Her hands are perpetually dry from salt exposure. She dresses practically and shows no interest in softening her appearance for visitors.

**Manner:** Self-sufficient to the point where asking for help requires a visible act of will. She states things plainly when she trusts the listener. She does not state things at all when she doesn't. Initial encounters with the players will be brief and evaluating — she's deciding whether they're useful.

**Wants:** To keep her family's charter intact and her children safe, specifically from a threat she cannot fully name or prove. She wants the source of her unease identified and removed, and she wants to understand what the Cabal actually wanted from her property.

**Secret:** She does not know about Pell's correspondence with Lore. When she finds out — and she will, the moment players surface this — her first response will be very quiet and very controlled in a way that should read as more frightening than shouting.

---

### Davan Lore (Cabal Agent)

**Appearance:** Thirties, well-dressed for a man supposedly traveling on merchant business, speaks with the measured cadence of someone who has practiced patience as a skill. His hands are soft. He smells faintly of cedar.

**Manner:** Reasonable. Never threatens directly. Has a gift for making whatever he wants sound like the obvious practical choice for everyone involved.

**Wants:** Uncontested access to the underground chamber beneath the eastern boiling house, ideally through a property transfer but now through leverage on Pell.

**Secret:** He is aware the players may be investigating Emm's situation. He will not confront them directly. He will accelerate his timeline — contacting Pell and making the scholarship offer within days of the players' arrival becoming known to him.

---

## RUNNING IT

> *The Kettermere salt-works is three boiling houses and a sorting yard built around a natural brine pool whose saltiness hasn't diminished in four generations of production. Emm Crane is in the sorting yard when you find her, supervising two workers without appearing to watch them. She turns when you approach and gives you the specific look of someone who has been interrupted by many people recently and found all of them insufficient. "If you're buying salt," she says, "talk to my foreman. If you're here about something else, you have two minutes to make it worth my time."*

**Beat 1 — Building Trust.** Emm does not volunteer information. Players must demonstrate they have legitimate reason to ask — Sentinel Order credentials, a Loyalist referral, or simply evidence that they know about the Cabal and can speak to it usefully. A DC 12 Persuasion check combined with relevant faction knowledge opens the full conversation. Without that, she'll confirm the basics and nothing more.

**Beat 2 — The Boiling House.** If players investigate the eastern boiling house, they find the sealed chamber. What's below it is old — ritual preparation that predates the current Cabal by centuries. The Cabal didn't create this convergence; they found it. This context matters for how Emm processes the situation: her family has been sitting atop something significant without knowing it for forty years.

**Beat 3 — Pell.** If players surface the correspondence, Emm goes very quiet for about ten seconds and then asks one question: "Does he know who he's been writing to?" The answer is no. Her next question: "Where is he right now?" From here, the immediate priority shifts from the salt-works to getting a message to Pell before Lore makes his move.

| DC | Skill | Outcome |
|----|-------|---------|
| 10 | Persuasion | Emm confirms the two refusals; explains the silence concern |
| 13 | Insight | Recognizes that Emm knows more than she's saying about Pell |
| 15 | Investigation (boiling house) | Locates sealed chamber; verifies ley-line significance |
| 16 | Arcana (chamber examination) | Identifies ritual preparation; establishes Cabal's actual objective |
| 18 | Persuasion (with Pell evidence) | Emm fully cooperates, shares all correspondence she's found, agrees to faction protection |

**Resolution Path A — Expose and Intercept.** Players locate Pell in Aranthor, surface the correspondence, and get him clear of Lore's influence before the scholarship offer is made. Lore loses his leverage. Emm establishes a Crown Loyalist protection arrangement. The chamber is sealed with proper warding. The Cabal loses the node but marks the party as directly responsible.

**Resolution Path B — Remove Lore.** Players locate and remove Davan Lore from the coastal district — arrest, exile, or other means. Without his handler, the local Cabal cell loses operational capacity in the area. The chamber remains a long-term vulnerability. Emm is grateful but pragmatic about what "grateful" means from her.

**Resolution Path C — Faction Protection.** Players arrange for a Sentinel Order or Crown Loyalist officer to formally designate the salt-works as a protected site. This doesn't resolve the chamber but makes Cabal acquisition politically costly. Lore's approach shifts to longer-term undermining rather than direct leverage.

---

## IF IT FAILS

**Outcome A — Pell Enrolled.** Lore makes the scholarship offer and Pell accepts before the players intervene. Emm faces a choice: resist and risk Pell's opportunity, or allow Cabal access to the chamber. This becomes a longer arc with Pell's welfare as an ongoing pressure point.

**Outcome B — Cabal Acquires the Works.** Emm, under sustained pressure, sells. The convergence point becomes active within one season. The coastal district begins experiencing minor but measurable disturbances — fishing catches drop, the brine pool's output changes quality, workers at the new management report unusual sounds from the eastern boiling house at night.

---

## REWARDS

- Emm's ongoing cooperation as a coastal district contact and information source
- 80gp in salt-trade credit (redeemable at any Kettermere trading partner)
- Sentinel Order commendation if the chamber is properly sealed (minor credential)
- Pell as a future ally if his connection to Lore is resolved cleanly

---

## CONNECTS TO

- **The Sealed Strongbox at Warden's Ford** — Cabal operational funding is relevant; loss of the Warden's Ford funds slows escalation
- **What the Thornkeeper Saw** — the ley-line network across Aranthor is a connected system; the Kettermere node is one piece
- **The Cabal Offer at Kettermere** closes a gap in the Cabal's coastal ritual presence; failure here accelerates their Thornmere timeline
