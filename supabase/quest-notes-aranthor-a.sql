# What the Thornkeeper Saw
### *She went into the central channels. She came back. She will not say what she saw. She has been sending her grandchildren west.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Fernholt, Thornmere central channels |
| **Level Range** | 1–5 |
| **Duration** | 60–90 minutes (plus optional Thornmere expedition) |
| **Quest Giver** | Thornkeeper of Fernholt |
| **Quest Type** | Regional — Shadow Cabal Investigation, Rescue Arc |

---

## THE SITUATION

The Thornkeeper of Fernholt is the oldest person in the village and has navigated the Thornmere's channels since she was seven years old. Three weeks ago she went into the central channels alone to investigate reports of dead fish and silent Merefolk villages. She came back two days later. She has not described what she saw to anyone. She is moving her grandchildren west one by one, staging them with distant relatives, giving no explanation that satisfies anyone.

Fernholt has noticed. The village's relationship to the Thornkeeper is one of practical dependence — she knows which channels are safe, which thorn-willow growths are navigable, and which areas flood in heavy rain. Her silence about something she clearly considers serious has generated the kind of low-grade fear that a village generates when it cannot identify what to be afraid of.

The Mereford Inn's proprietor, Holt Cass, has been asking his own questions. He runs a trade route that crosses three Merefolk villages in the central channels and none of his last four trade parties have made successful contact.

---

## BACKGROUND

*(DM-only — do not share with players)*

The Thornkeeper saw a Shadow Cabal ritual site constructed on a permanently submerged platform in the central channels — a broad flat structure, three feet below the waterline, invisible from the surface under normal conditions. The site is covered by thorn-willow growths that were deliberately cultivated for concealment; the Cabal has been maintaining this site for at least two seasons. There is no dry-land access. You reach it by water only, through a specific channel route that the Thornkeeper knows and no one else in Fernholt does.

Three Merefolk villages in the central channels — Deepwallow, Brinereach, and the smaller settlement called the Floating Moot — have been coerced into providing labor for the site's construction and maintenance. The coercion is not violent: the Cabal is holding a warding stone that prevents a cyclical Thornmere flooding event from destroying all three villages. The villages know the Cabal is Shadow-adjacent. They are not willing collaborators. They are afraid.

The Thornkeeper will not speak publicly because she believes that if she tells outsiders before there is a plan to act, the Cabal will learn she has spoken (they have a contact in Fernholt, a toll-collector named Wen Brosse, whom she doesn't know about) and the response will fall on the Merefolk. She is not protecting herself. She is protecting three villages that cannot protect themselves.

She will guide the party if they demonstrate: (1) they understand what they're being asked to face, not just that something is wrong; (2) they have a plan for protecting the Merefolk during and after intervention; and (3) they can present her with evidence they are not Blackwell-aligned — she doesn't trust military solutions here.

---

## KEY NPCs

### The Thornkeeper of Fernholt

**Appearance:** Mid-seventies, small, moves through the village with an unhurried certainty that makes her seem taller. Her hands are mapped with old thorn-scratches healed to white lines. She doesn't sit when she talks to people — she stands and makes the conversation efficient.

**Manner:** Economical with words. Listens more than she speaks. When she does speak it lands with a specificity that makes clear she's been thinking about it before opening her mouth. She is not unkind; she is simply not interested in excess.

**Wants:** For someone to actually be able to do something before she describes what is at stake. She has learned not to describe problems to people who cannot help — it only creates panic and draws attention. She is holding a very large weight with a great deal of muscle control and she is looking at the players to see if they can take any of it.

**Secret:** Went into the central channels and found the ritual site. Observed Cabal operations for approximately six hours before withdrawing. Has detailed information — site layout, approximate number of operatives, Merefolk involvement, ritual progress stage — that she will not share until she believes sharing it is net positive. The evacuation of her grandchildren is her contingency plan for if she's wrong about the players.

---

### Holt Cass (Mereford Inn)

**Appearance:** Fifties, heavyset, runs the inn with the organized energy of someone who has converted anxiety into systems. His trade records are meticulous. He knows everyone who moves through the Thornmere district.

