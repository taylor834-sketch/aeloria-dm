## KEY NPCs

### Coll Sarne

**Appearance:** Early sixties, the lean and weathered look of someone who has spent forty years in the Greywood. His clothes are practical and old. His boots are resoled twice over. He keeps his documents in a case he's carried since his first charter was signed.

**Manner:** Methodical and direct. He prefers to resolve things through systems — contracts, charters, legal procedures — and has a genuine faith in documented process as a form of justice. He is not warm with strangers but is honest with them. He respects competence regardless of affiliation.

**Wants:** For Blackwell to operate within the existing charter framework. For the Greywood to remain viable. For whoever governs Aeloria to recognize that thirty-year charters represent legitimate property rights that do not dissolve during succession crises. He wants the northern situation understood and managed before it reaches his operation.

**Secret:** Has been rerouting drainage channels that compromise Blackwell's lumber seasoning. Fort Ashveil is being built with structurally compromised timber. He views this as legal and proportionate; he will defend it on those terms if surfaced. He has no guilt about it. He has some worry about what happens if Blackwell discovers it before the succession question is resolved and the legal framework is functioning again.

