## RUNNING THE QUEST

> Haleth's office is small and functions entirely. No decorative objects. A map of the Marches on one wall with marks that are not explained. She doesn't stand when you come in — she gestures to the chairs and waits until you're settled. Then: "I need something burned. A building, fourteen miles east. I'll give you the location. I'm not going to tell you why yet, and I need to know whether that's a problem."

**Beat 1 — The Ask.** Haleth explains only what is necessary: the building's location, that it contains cult materials and no people, and that she will provide full context afterward. She is asking the players to act on trust. Insight DC 12: she is not concealing malice — she is concealing vulnerability, which is something different. If players push for context before agreeing, she gives them one thing: "There is a girl in an outer settlement who needs to come home first. The warehouse is the leverage. I need to know the leverage is real before I go to the table." This is enough.

**Beat 2 — The Negotiation at the Warehouse.** Players travel to the warehouse and locate Marn. He is present; he is managing a supply inventory when they arrive. He is not surprised someone has come — Haleth's patience has been communicating itself to the cult's logistics chain for two months, and he has been waiting for an escalation. The players make the case: one teenager, not worth a burned warehouse and Sentinel Order formal attention. Marn consults nothing. He says: "Three days. The girl is in the outer settlement. Give me three days to arrange the transfer without incident." Persuasion DC 13 to close the deal; failure means he asks for five days and an agreement that the Sentinel Order is not notified for thirty days after the transfer.

**Beat 3 — Sera's Return and the Warehouse.** Three days later, Sera walks into Ashval at midmorning. She is alone, which she requested. She goes directly to Haleth's office and closes the door. Players are not present for this. That evening, a fire is visible to the east — controlled, not spreading, producing a column of smoke that anyone in Ashval can see. Haleth finds the players before the smoke clears. She gives them the supply route records without preamble. She says: "Don't tell anyone I did this sentimentally. I have a reputation." She does not wait for a response.

