| 14 | Arcana or Investigation | The warehouse contents confirm Haleth's assessment — nothing ambiguous inside |

**Resolution Path A — Trust first, context after.** Players agree to Haleth's initial ask without demanding full context. She gives them more context than she would have otherwise — including the names of the two teenagers already recovered and the length of time she has been running this. She is not visibly moved by their trust. She is marginally more forthcoming.

**Resolution Path B — Negotiate for context first.** Players require full context before proceeding. Haleth provides it, correctly and completely, but the relationship is defined as transactional rather than trust-based. She will work with the players on future Ashen Marches matters through Ashval's formal channels. She will not contact them directly again.

