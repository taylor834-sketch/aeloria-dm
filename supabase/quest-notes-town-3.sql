# The Six-Month Widow
### *Kael Nast was declared dead eight months ago. Yesterday he walked off a boat.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Shattered Isles — Saltmere |
| **Quest Giver** | No formal giver — the situation presents itself |
| **Difficulty** | No combat; no skill checks that solve the problem |
| **Duration** | 1 session (with aftermath reported next session) |
| **Themes** | Ulraeth time loops and human cost, grief, the difference between eight hours and eight months |
| **Leads To** | Fog and Freedom (personal stakes), Wraith Galleon (same mechanism) |

---

## THE SITUATION

Eight months ago, Kael Nast sailed out of Saltmere on a small fishing sloop and did not come back. The Shattered Isles fog took him — or so everyone assumed. His wife Cora filed the paperwork, settled his debts, sold the sloop he had half-paid for, and spent eight months doing the quiet, brutal work of building a life that had Kael's absence in it instead of Kael. She is not the same person she was the morning he left. She doesn't know if she is better or worse, but she is different, and the difference is hers.

Yesterday morning, Kael walked off a fishing charter that had pulled him from the water near the Isles. He is in perfect health. He is confused about why Cora won't immediately come to him. To him, he left Saltmere this morning. He went out, hit fog, the day got strange, and now he's back and it's apparently been eight months and he doesn't understand why his wife is at the door instead of in his arms. He is not wrong to be confused. She is not wrong to need time. The players encounter both of them, separately, within a few hours of Kael's return.

---

## BACKGROUND

Kael was caught in a Ulraeth time loop — a minor one, single-day, nothing dramatic. He experienced eight hours of grey water and disorientation and then came out the other side, physically unchanged, emotionally fine, eight months displaced. He has no special knowledge of what happened to him beyond the fog and the strange quality of the light. He cannot tell the players anything useful about the loop mechanism. He is just a fisherman who went out and came back and found that the world had moved on without him.

Cora's situation is harder. She did everything right. She grieved correctly, by her own account: let herself feel it, asked for help when she needed it, found her footing slowly. She is not still in acute grief. She is in the part after grief where you have rearranged yourself around the loss. Kael's return does not undo eight months. It does not restore the version of her that existed before she had to learn to live without him. She is relieved — genuinely, physically relieved, the kind that makes her hands shake — and she is also furious and confused and guilty about both. She needs approximately three days before she is ready to sit at a table with him. She will get there. She needs the players to understand that this is not a failure.

The players' role in this quest is not to solve anything. It is to be present, separately, for two people who cannot talk to each other yet and who need someone who isn't each other.

---

## KEY NPCs

**Kael Nast**
- *Appearance:* Weather-worn, forty, looks like a man who spent a normal day at sea and came home to find his house rearranged. Keeps touching things — the dock rail, the wall of the harbormaster's shack — like he's confirming they're real.
- *Manner:* Warm, confused, trying to be patient with something he doesn't have the framework to understand. He does not perform distress — he's genuinely fine, and the fineness sits wrong against the situation.
- *Wants:* His wife. In lieu of that, someone who will help him understand what eight months means from her end.
- *Secret:* He is starting to suspect, slowly, that it isn't just Cora who changed. He is the same person who left. He will have to become a different person to live in the life that has continued without him. He hasn't named this yet.

**Cora Nast**
- *Appearance:* Thirties, steady hands except for right now, dark circles from last night. The kind of person who is competent at everything and very rarely lets anyone see what it costs.
- *Manner:* Controlled, with effort. She does not want to perform grief or relief for anyone. She is deciding what she feels at her own speed and she would like people to get out of her way while she does it.
- *Wants:* Time. Not days — she means today. She means this afternoon. She means she needs to go for a walk by herself before she can speak to him without the shaking.
- *Secret:* She is already thinking about the meal she will bring him. His favorite — salt-fish and hard bread, the way he takes it with too much pepper. She knows exactly when she will bring it. She will not tell anyone this until after.

---

## RUNNING THE QUEST

> You hear about Kael Nast before you meet him. Three people on the dock mention it — the fisherman who came back from the fog, yes, the one whose wife just had his death-day last month — the way small towns process something that doesn't fit. By the time you find him, he's sitting on a coil of rope outside the harbormaster's shack looking at the street with the expression of a man who left his house for an hour and returned to find all the furniture changed.

This quest runs as two conversations, one with Kael and one with Cora, played back to back or intercut at the DM's discretion. There are no skill checks. There is no villain. There is no resolution the players can provide.

**What Kael needs to hear:** That eight hours is not the same as eight months. That her needing time is not a rejection. That she has not replaced him in her life — she has survived the loss of him in her life, which is different, and the difference matters. He cannot expect her to immediately un-survive it just because he's back. He can say "I understand" without understanding yet, and that is enough for right now.

**What Cora needs to hear:** That she is allowed to be relieved before she figures out anything else. That relief and grief and resentment can all exist at the same time and none of them are wrong. That she does not have to perform "wife whose husband just came home" on anyone's schedule. That she did everything right and the situation being hard anyway is not her fault.

**The resolution:** Three days later, someone mentions to the players — casually, in passing — that they saw Cora at Kael's door with a plate of food. That's all. That's the whole resolution. Players don't see it directly.

There are no dice rolls for this quest. If players try to roll Persuasion to "convince" either NPC, let them roll — and then let the NPC respond as a real person, not a DC target. Cora will thank them politely and close the door. Kael will nod and continue looking at the street.

---

## IF IT FAILS

**Players try to fix it instead of witness it.** They pressure Cora to speak to Kael before she's ready, or they give Kael a plan for "winning her back." The conversation happens too soon. It's stilted and painful and they both feel worse. The resolution still happens — Cora is a practical woman and she knows what she wants — but it takes two weeks instead of three days, and it's harder.

**Players are dismissive or transactional.** They take the information about the fog loop from Kael and leave. Saltmere notes this. Not formally — nobody files a report. But the town's warmth toward the players drops a register, and Cora, when they meet her later in other contexts, is professionally courteous and nothing more.

---

## REWARDS

There is no material reward for this quest. That is the point of this quest.

What the players gain: a standing in Saltmere that cannot be bought or factionalized. People who were treated like people, not like quest objectives, remember it. If players return to Saltmere in future sessions, Kael and Cora will go out of their way. Not in a mechanical, transactional sense — in the sense that they know the players' names and they use them.

The quest also serves as a direct human illustration of what Fog and Freedom means as a campaign. The Wraith Galleon runs the same mechanism at scale. When the players finally board it, they will have met someone for whom this was not an adventure.

---

## CONNECTS TO

- **Fog and Freedom** — Ulraeth's time loops rendered as human cost rather than dungeon mechanic
- **Wraith Galleon (dungeon)** — the same displacement mechanism, at scale; Kael's experience is a single-person version of what happened to the Galleon's crew
