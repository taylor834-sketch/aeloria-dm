# The Child on the Road
### *She knows every trail in the Ashen Marches. She doesn't know her own name.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Ashen Marches — Ashval |
| **Quest Giver** | Petra, Ashval's healer (concern); townsfolk (context) |
| **Difficulty** | Low combat (if any); negotiation-focused |
| **Duration** | 1–2 sessions |
| **Themes** | Void seepage and memory, Bloodmire Cult periphery, the grey zone as navigation tool |
| **Leads To** | The Hungry Dark (grey zone route, Bloodmire cult), Ashval-Haleth (same region, same cult) |

---

## THE SITUATION

Three days ago, a child appeared on the eastern road into Ashval. She is perhaps eight years old, travel-worn but physically unhurt, with no memory of her name or family. What she does have is an exact, comprehensive knowledge of the Ashen Marches — every trail, safe camp, water source, and grey zone boundary within thirty miles. She navigated herself into Ashval without hesitation, from the direction of the worst terrain in the region, without a map. When she was asked where she came from, she described the route but not the origin. She has nightmares every night and will not describe them when she wakes.

Petra, Ashval's healer, has housed her and has done everything medically available. The child is not ill. She is not traumatized in any conventional sense. She is quiet and watchful and very good at orienting herself in space at all times. She keeps track of exits. Petra is worried about the nightmares and more worried that the child seems to consider them simply a thing that happens now, like weather.

---

## BACKGROUND

Her name is Mira. She will not have this information until near the end of the quest, when someone from her family confirms it. She doesn't resist the name — she simply has no connection to it.

Mira's family came to the Ashen Marches as refugees three months ago, part of a slow westward migration driven by blight conditions further east. They settled in a small community near the outer edge of Bloodmire Cult territory — not as cult members, but as people who needed shelter and accepted the conditions that came with the nearest available shelter. The cult's outer settlements operate as functional communities. There is no daily horror. There is proximity to something that produces daily horror elsewhere, and the residents have learned not to look in certain directions.

Six weeks ago, Mira followed a wounded animal into the grey zone. The void seepage in the grey zone strips cognitive load — anxiety, active memory, the mental hum of ongoing concern. For adults this is disorienting and dangerous. For a child who was anxious about getting lost, it removed exactly the thing that would have gotten her killed, leaving her with perfect spatial orientation and no memory of anything before the grey zone's edge. She navigated herself to Ashval over four days, sleeping in the safe camps she instinctively knew were safe, eating from the water sources she instinctively trusted.

The nightmares are not caused by the void. They are the fractured remnants of her pre-grey-zone memories trying to surface through damaged recall. They will diminish when she is returned to context — to her family, to familiar objects. Her grey zone navigation knowledge is not erased when her memory returns. It is now part of how she is wired.

**Getting her home** requires locating the family and then extracting her — or negotiating her release — from a situation involving Bloodmire Cult proximity. There are two workable paths.

---

## KEY NPCs

**The Child (she names herself "Grey" when pressed)**
- *Appearance:* Eight years old, brown hair still tangled from the road, practical eyes. The kind of child who looks at a room's exits before she looks at the people in it.
- *Manner:* Quiet but not withdrawn. Answers questions directly. Does not ask questions she doesn't expect useful answers to.
- *Wants:* She cannot articulate what she wants. When Petra asks, she says "I think I'm looking for something." She does not know what.
- *Secret:* She remembers the grey zone in perfect sensory detail — color, temperature, the sound of her own footsteps. She does not know why these memories are more vivid than everything else. She finds this interesting rather than frightening.

**Petra, Ashval's Healer**
- *Appearance:* Compact, fifties, the kind of competence that doesn't need to announce itself. Her workroom has too many dried plants in it and she knows exactly where each one is.
- *Manner:* Practical care. She has been doing everything right for this child and she would like someone to now do something she cannot.
- *Wants:* A name, a family, and a reason the nightmares will stop.
- *Secret:* She tested the child's geographical knowledge yesterday — drew a rough map, asked her to correct it. The child corrected twelve errors without hesitation. Petra has not told this to anyone because she is still deciding what it means.

**The Bloodmire Settlement Leader (Arveth)**
- *Appearance:* Fifties, steady, the kind of grey in his beard that comes from management rather than age. Wears no cult insignia in the settlement proper.
- *Manner:* Practical and unsentimental. He runs a community of eighty people in a difficult situation and he does not have time for ideology or drama.
- *Wants:* No trouble. No attention. The settlement to keep functioning.
- *Secret:* He has been quietly cataloguing families who are not cult-committed for eventual extraction — his own, eventually. He has been doing this for two years. He will not tell the players this, but he will cooperate with a reasonable ask because his own long-term plan benefits from owing someone a favor outside the settlement.

---

## RUNNING THE QUEST

> Petra meets you at the door before you've fully knocked. "I heard you were asking about her," she says. "Come in. And don't make it into something large — she's had enough large." The child is at the workroom table sorting dried herbs by color into small piles, methodical and unhurried. She looks up when you enter. She notes both exits before she looks at your faces. She does not look afraid.

**Beat 1 — The Child's Account.** Grey will answer questions about the grey zone with precise geographic detail. She will describe the routes she took, the camps she used, and the quality of the air at the zone's edge. She will not be able to explain how she knows any of this. If asked about her family, she describes a community — wooden buildings, a well in a central square, a man who grew beans in raised beds — but she cannot attach names or faces to the description.

**Beat 2 — Locating the Settlement.** Grey's description of the settlement, combined with Petra's notes and any Ashen Marches knowledge the players carry, places the community approximately fourteen miles east, within the outer cult territory. Investigation DC 13 to triangulate the location from her description. A ranger or character with Survival can reduce travel time through the grey zone using Grey's route knowledge (she will draw them a map without being asked).

**Beat 3 — The Negotiation.** Players arrive at the settlement and find Mira's family — her mother Senna, her father Tomas, her brother who is eleven and has not slept well since she disappeared. Arveth will speak to the players before they reach the family. He is not hostile. He wants to know why they're here and what they want. The ask — return of the child to her family — is one he will grant without conditions if approached without aggression. If players come in formally or with obvious Sentinel Order affiliation, he is more cautious but still cooperative. The family's reunion is immediate and quiet. Senna holds her daughter for a long time and does not say anything. Mira does not cry. She presses her face into her mother's shoulder with the focused attention of someone confirming a memory.

| DC | Skill | Outcome |
|----|-------|---------|
| 13 | Investigation | Triangulate settlement location from child's description |
| 12 | Insight | Arveth will cooperate — he is practical, not ideological |
| 15 | Persuasion | Arveth shares context about the cult's outer settlement structure (useful for Hungry Dark) |
| 10 | Survival (ranger) | Navigate the grey zone using Grey's route map; no void exposure risk |
| 14 | Medicine | The nightmares are memory fragments, not void damage; context will help |

**Resolution Path A — Direct family extraction.** Players negotiate Mira's return, the family comes with her willingly (they were already planning to leave), and Arveth lets them go without obstruction. Players leave with the grey zone safe route, the family's gratitude, and a setup for Bloodmire Depths via Arveth's guarded cooperative manner.

**Resolution Path B — Alert the Sentinel Order.** Players report the settlement location to Sentinel Order contacts. A formal extraction is organized. This works but takes longer, puts the players in a bureaucratic role rather than a human one, and Arveth will not be cooperative in future encounters. The family is extracted safely. Grey's route knowledge is formally documented by the Order.

---

## IF IT FAILS

**Players don't locate the settlement before Grey's nightmares escalate.** After ten days without contextual grounding, the memory fractures worsen. Grey is not in danger of permanent damage, but she becomes unreachable for a week. The quest pauses rather than ends — the family is still there, the route is still recoverable, but the window of easy resolution closes.

**Players approach Arveth aggressively or with Sentinel Order backing he finds threatening.** He moves the family before players can reach them, covering his own tracks efficiently. The grey zone route is still obtainable — Grey still knows it — but the Bloodmire cult intelligence angle (Arveth as a potential inside contact) is lost.

---

## REWARDS

- **The grey zone safe route** — the best existing navigation path through the most dangerous approach to the Hungry Dark's territory. Worth more than any existing map. Rendered as an actual hand-drawn map if players have Grey draw it for them.
- **The family's gratitude** — Senna and Tomas are not powerful people, but they know the eastern Marches and they will share that knowledge freely with people who brought their daughter home.
- **Bloodmire cult structural intelligence** — if Arveth was handled well, players have a firsthand account of outer settlement operations and an implied future contact.

---

## CONNECTS TO

- **The Hungry Dark** — grey zone safe approach route; Bloodmire Cult outer settlement structure; Arveth as a potential inside contact
- **Ashval-Haleth** — same region, same cult, same grey zone; Haleth's quest and this one can be run in sequence or simultaneously
