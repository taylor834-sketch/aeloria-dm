# The Ship That Smiled
### *Something happened to the Ardent Wave. Nobody can explain why the crew look so content.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Sapphire Coast — Dawnharbor |
| **Quest Giver** | Harbormaster Edric Vane |
| **Difficulty** | Low combat, high investigation |
| **Duration** | 1 session |
| **Themes** | Ulraeth time loops, investigation, reliable contact acquisition |
| **Leads To** | Fog and Freedom (fog coordinates), Wraith Galleon (same mechanism) |

---

## THE SITUATION

The cargo ship Ardent Wave came into Dawnharbor's east dock this morning under full sail, crew at stations, in perfect order. No damage. No distress flags. No cargo. No log entries for eight days. And every single crew member — captain, first mate, deckhands, cook — is smiling. Not the empty smile of a charmed or dominated person. A genuine, warm, contented smile, the kind a person wears after a week of good sleep and nothing going wrong. It is the most unsettling thing Harbormaster Edric Vane has seen in twenty-three years managing this port, and he has seen ships come in crewed by the dead.

The Ardent Wave left Dawnharbor twelve days ago bound for the Thornport trading post, a route the crew knows cold. They should have been back in four. Edric has kept the crew aboard and the dock quiet, but the port authority inspector is scheduled tomorrow morning, and an empty-logged ship with a suspiciously cheerful crew is the sort of thing that generates paperwork nobody wants. He needs the players to go aboard tonight, speak to the crew, and come back with something that sounds like an explanation.

---

## BACKGROUND

The Ardent Wave sailed into a Ulraeth fog zone off the Shattered Isles — the same zone documented in no official chart, the same zone Fog and Freedom will bring the players back to. Inside it, the ship ran a time loop for eight subjective days: one good day, repeated. Fair winds. A decent catch when the cook threw a line. No incident. The loop reset each morning just before sunrise, and none of the crew consciously noticed the reset — they simply woke up, had a pleasant day, and slept well.

The smiling is a residual Ulraeth effect called a contentment echo — a harmless psychic imprint from eight consecutive repetitions of the same pleasant emotional state. It fades within a day of leaving the fog zone. While it lasts, the crew is incapable of deception: not magically compelled to truth, simply too relaxed to bother with dishonesty. They will answer every question fully and with an air of pleasant calm. Their memories are intact and precise: fog coordinates, the exact weather pattern, the moment the horizon turned wrong, the smell of the air just before the loop reset. The first mate Dorin has described this as "objectively, a good week. I would like to not repeat it, but I have no complaints about the week itself."

The cargo hold is empty because the ship never reached Thornport to pick anything up. The log entries stopped when the fog hit — not because anyone chose to stop writing, but because nothing happened that needed recording. The same pleasant day, eight times over, seemed like one entry should cover it.

---

## KEY NPCs

**Captain Maren Sull**
- *Appearance:* Practical build, salt-weathered, dark hair going grey at the temples. Still in her sailing coat, which she hasn't taken off since docking.
- *Manner:* Professionally unsettled. She is a competent woman in an incompetent situation and the combination is producing a quiet, focused displeasure.
- *Wants:* An explanation she can write in the log. Something that makes sense. She is also, privately, trying not to notice how rested she feels.
- *Secret:* She has the fog coordinates memorized and hasn't told Edric. She isn't sure why she's holding them back. It feels like information worth knowing who to give it to.

**First Mate Dorin**
- *Appearance:* Broad-shouldered, cheerful crow's-feet, currently sitting on a coil of rope looking at the harbor lights with an expression of mild benevolence.
- *Manner:* Answers every question fully, helpfully, and with a small smile. Not manic — just genuinely unbothered. He is aware this is unusual and finds it faintly interesting.
- *Wants:* Nothing urgent. He will want a meal and his bunk when this wears off.
- *Secret:* He counted the resets. He knows it was exactly eight. He doesn't know what that means but the number feels important.

**Harbormaster Edric Vane**
- *Appearance:* Fifties, ink-stained fingers, the look of a man who has been awake since the ship docked and has drunk three cups of something he's calling tea.
- *Manner:* Wants this resolved quietly and quickly. He is a good man who is also a bureaucrat, and the combination means he needs a story, not necessarily the whole truth.
- *Wants:* Something to write down that makes the port authority go away. Not a cover-up — just a plausible frame.
- *Secret:* He's seen one other ship come in like this, years ago. He filed it as "crew illness, resolved." He has never told anyone.

---

## RUNNING THE QUEST

> The Ardent Wave sits quiet in the east dock, lanterns lit at bow and stern, crew visible through the rigging. They aren't hiding. They aren't anxious. Two deckhands at the rail are watching the harbor in comfortable silence, and when one of them spots you coming up the gangplank, he raises a hand in a friendly wave. Not urgent. Not nervous. Just — friendly. The harbormaster, walking behind you, says under his breath: "You see what I mean."

**Beat 1 — The Deck Interview.** The crew is cooperative and calm. Initial questions reveal the timeline gap immediately — the crew has no awareness anything unusual occurred. Good day, good winds, they're home. The log gap is discovered when Dorin brings out the log book himself without being asked: "I noticed we didn't write much down. It seemed like a quiet trip." Perception DC 12 to notice the smiles are synchronized — every crew member's resting expression lands in the same emotional register.

**Beat 2 — The Captain's Account.** Maren will speak privately if asked. She has the fog coordinates and she gives them if the players seem like the kind of people who will do something useful with them. She describes the fog zone in specific geographic terms: its position relative to the Shattered Isles, the color of the water at its edge, the way the compass behaved (it didn't spin — it just agreed with whatever direction they turned). Arcana DC 14: Ulraeth signature. History DC 16: consistent with fog anomalies documented by Sentinel Order researchers in the third age.

**Beat 3 — Dorin's Count.** If asked directly, Dorin confirms eight resets. He knows because he's a methodical man and he kept a scratch-tally in his boot. "One good day. Eight times. I would like my wife to know I was not having a good time on purpose."

| DC | Skill | Outcome |
|----|-------|---------|
| 12 | Perception | The smiles are synchronized across all crew — same baseline affect |
| 14 | Arcana | Residual Ulraeth energy; contentment echo; non-harmful, temporary |
| 13 | Investigation | The log gap is clean — not torn, not blotted, just absent, like time moved without requiring notation |
| 15 | Insight | Maren is holding something back and will give it freely to someone who asks directly |
| 16 | History | Documented fog anomaly type, Shattered Isles region, Sentinel Order records |

**Resolution Path A — Provide a frame for the harbormaster.** The players summarize the fog event in terms Edric can write down. "Navigational anomaly, fog disorientation, crew recovered." He accepts this, thanks them, and quietly makes sure they have a standing invitation to use his dock facilities. Captain Sull, separately, provides the fog coordinates and says: "If you go looking, be careful about counting your days."

**Resolution Path B — Take the intelligence directly.** Players ask for the full technical account before reassuring Edric. They leave with the fog coordinates, Dorin's reset count, the compass behavior notes, and Maren's private geographic description. This is a complete field report on the fog zone's location and behavior, better than anything currently in the players' possession. The harbormaster gets his frame, and nobody is worse off.

---

## IF IT FAILS

**The crew sobers up before the players get answers.** The contentment echo fades and Maren's professional caution returns. She is no longer forthcoming. She will share the fog coordinates only if the players have established sufficient trust — or if they've helped her with something else first.

**The port authority arrives early.** The inspector is not hostile, but he is thorough, and a ship with an eight-day log gap needs explaining. Players who haven't built rapport with Edric get no cover, and the investigation becomes official — complicating any future use of Dawnharbor as a home port.

---

## REWARDS

- **Fog zone coordinates** — precise geographic location with compass behavior notes and reset-interval timing. Direct intelligence for Fog and Freedom.
- **Captain Maren Sull as a contact** — she owes the players a discreet handling and she is reliable. She runs a clean ship and she remembers a favor.
- **Harbormaster Edric's goodwill** — use of east dock facilities, quiet docking rights, and someone who will file things diplomatically on the players' behalf.

---

## CONNECTS TO

- **Fog and Freedom** — the fog coordinates are a primary intelligence objective for that campaign; this quest delivers them at low stakes
- **Wraith Galleon (dungeon)** — the same Ulraeth loop mechanism operates at scale aboard the Galleon; crew testimony here foreshadows what the players will find there
