## IF IT FAILS

**Marn refuses the deal or the negotiation goes badly.** The timeline extends. Haleth goes back to her original leverage arrangement — slower, less effective — and Sera stays in the outer settlement for another two months. The warehouse does not burn. Haleth is not angry with the players; she simply proceeds without them. They do not receive the supply route records.

**Players notify the Sentinel Order before the transfer is complete.** A formal extraction is organized. It goes poorly — the Depths entrance is sealed, the outer settlement locks down, and Sera is moved deeper. Haleth's years of quiet negotiation are destroyed in an afternoon of official action. She will tell the players, once, exactly what they cost. She will not raise her voice.

