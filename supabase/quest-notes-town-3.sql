# Elder Haleth's Favor
### *The mayor asks you to burn a building. She will explain after.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Ashen Marches — Ashval |
| **Quest Giver** | Mayor-Elder Haleth (directly, by name, quietly) |
| **Difficulty** | Negotiation-focused; surface encounter, not dungeon combat |
| **Duration** | 1–2 sessions |
| **Themes** | Bloodmire Cult intelligence, leverage and patience, the cost of quiet competence |
| **Leads To** | The Hungry Dark (supply routes, cult structure), Ashval-Child (same region, same cult) |

---

## THE SITUATION

Mayor-Elder Haleth sends for the players specifically. Not through an intermediary — a short note, your names on it, a time and a place. When they arrive, she is alone, and she asks them to burn a building on the Marches' eastern edge. She gives them the location. She does not give them a reason. She says: "I will explain after. I need to know first whether you are the kind of people who trust a question before they trust an answer."

The building is a Bloodmire Cult surface warehouse, approximately fourteen miles out. It is a real building that contains real cult supplies. She is correct that burning it will do no innocent harm. But she is asking the players to act on her word alone, without context, because the full truth of what she is doing is something she has not told anyone in six months, and she is not ready to say it out loud until she knows the players won't make it worse.

---

## BACKGROUND

Haleth has been running Ashval for twenty-two years. In that time she has watched the Bloodmire Cult expand its eastern territory by increments, absorbing refugee communities with the patient efficiency of something that doesn't need to rush. She is not afraid of the cult in a way that paralyzes her. She manages it the way she manages every other long-term threat: quietly, with leverage, without sentiment.

Six months ago, she acquired the cult's secondary supply route records — through means she will not detail, because the means are not the players' business. She did not report them to the Sentinel Order. She did not burn the warehouse immediately. She used the records as quiet negotiating currency with the cult's regional leadership: release the Ashval residents who had been recruited or pressured into the Depths, and the records stay private and the warehouse stays standing.

She got two back in four months. Oran Cassel, twenty, who came home thinner and quieter. Davan Marsh, nineteen, who has not spoken much since but is present and alive. The third is Sera, sixteen, taken eight months ago, the most recent and the most complicated because she was taken after the negotiation began and Haleth has been trying to get her back through the same channels with diminishing returns.

The warehouse burns when she has Sera. That is the exchange she is engineering. The players' role is to visit the warehouse — not the Depths — and make the case to the cult's surface liaison that holding one teenager is not worth a burned warehouse and the Sentinel Order's formal attention. It is a negotiation with a practical man who understands cost-benefit. It works, because the math is correct.

---

## KEY NPCs

**Mayor-Elder Haleth**
- *Appearance:* Grey hair, not styled for softness. The kind of face that has made difficult decisions often enough that it no longer performs difficulty. She has been awake since before the players arrived and she will be awake after they leave.
- *Manner:* Every word deliberate. She is not cold — she is someone for whom sentiment and currency are different things, and she does not mix them. She will not thank the players in words. She will give them the supply route records, which is a better currency.
- *Wants:* Sera home. The warehouse burned. Nobody finding out she did this for sentimental reasons, because she has a reputation for operating on logic and she would like to keep it.
- *Secret:* She knows all three of the recovered teenagers' families personally. She attended two of their naming ceremonies when they were infants. She will not say this. It is in her face, briefly, when she says Sera's name.

**Sera (when rescued)**
- *Appearance:* Sixteen, slight, dark circles, the contained quality of someone who has been making calculations for eight months about what to show and what to keep interior.
