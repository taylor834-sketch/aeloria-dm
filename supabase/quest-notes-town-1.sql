# The Missing Cartographer
### *She's fine. She's furious. She's also sitting beside something that might change everything.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Lighthaven, Northern Ridge |
| **Level Range** | 1–5 |
| **Duration** | 30–60 minutes (single session) |
| **Quest Giver** | Fen Dallow, Ren Voss's assistant |
| **Quest Type** | Town — Rescue, Investigation |

---

## THE SITUATION

Ren Voss, Lighthaven's only professional cartographer, set out four days ago to map the northern ridge approach — a routine survey she's done variations of a dozen times. She told her assistant Fen she'd be back by sundown of the second day. She is now two days overdue, and Fen has progressed from mildly worried to quietly panicked. He approached every person he thought might help and was turned away until the players.

The northern ridge isn't dangerous. That's what makes this strange. There are no large predators this time of year, no active weather, and Ren knows that ground better than anyone in Lighthaven. She carries water for three days and provisions for five. She is not the kind of person who gets lost. Fen says this last part a little desperately, like he's been saying it to himself for a day and a half.

What nobody in Lighthaven knows yet — including Fen, including the Sentinel Order — is that a ley-line intersection became active in that ridge sector sometime in the last two weeks. Ren found it. And Ren, being Ren, did not leave.

---

## BACKGROUND

*(DM-only — do not share with players)*

The ley-line intersection is real, new, and significant. It manifests as a circular depression roughly twelve feet across where the grass has simply stopped growing — not died, not burned, just absent, as though the ground decided it had other priorities. The air above it is measurably cooler and carries a faint harmonic resonance that most people would describe as a sound they can almost hear.

Ren sat down beside it on day one to take measurements. She has been there ever since, filling her third notebook. She ran out of water at the end of day two and is acutely aware of this, but the site has not stopped being fascinating enough to make her leave. She is annoyed that she didn't bring more water. She is more annoyed that no one has come to bring her any. When the players arrive she will accept water with a curt nod, drink half of it, and immediately resume explaining what she has found.

The deeper truth Ren doesn't yet understand: this ley-line activation is connected to the Crown's fracture. The Cracked Crown is not simply a political event — it disrupted the continent's underlying magical infrastructure, and dormant ley-line nodes are reactivating in sequence as that disruption propagates. Elyndra of the Sentinel Order would recognize this immediately if she saw Ren's notes. The players don't need to understand this now; they need to bring Ren's information to the right people eventually.

Finding the site requires a **DC 14 Investigation** check (tracking Ren's footprints and the subtle environmental tells of the depression — the absence of insects in the immediate area, a faint harmonic that becomes audible within two hundred feet).

---

## KEY NPCs

**Fen Dallow** — Ren's assistant, mid-twenties, organized in the way that people who work for chaotic geniuses become organized as a survival mechanism. Carries a spare notebook at all times. Will follow the players partway up the ridge if they let him.

- **Appearance:** Brown ink on his left sleeve from a pen that leaked three days ago. Hasn't noticed.
- **Manner:** Precise, earnest, and visibly relieved that someone is taking this seriously.
- **Wants:** Ren to come home. Also: to understand what she found, because Fen is secretly interested in cartography beyond the copying-fair-copies stage.
- **Secret:** He completed two of the maps in the last commission himself. Ren corrected them and said nothing, which is the closest she gets to a compliment.

**Ren Voss** — Lighthaven's cartographer. Compact, ink-stained hands, short silver-streaked hair she keeps tied back with whatever string is available. She speaks in precise measurements and exact directions. She has not slept more than four hours in two days.

- **Appearance:** Three notebooks visible, one open on her knee. A dry waterskin. Absolutely no sign of distress about her circumstances beyond mild irritation.
- **Manner:** Clinically focused. Responds to concern for her wellbeing by pivoting immediately to the thing she found. Not unkind — just genuinely does not understand why anyone would prioritize her comfort over this data.
- **Wants:** Accurate maps. Specifically: accurate maps of this site, which she is currently producing.
- **Secret:** She suspects this ley-line node is connected to the Crown's fracture and has been afraid to write that down because she doesn't understand magic well enough to be sure, and Ren does not commit uncertainty to permanent record.

---

## RUNNING THE QUEST

> *Fen Dallow meets you near the northern market, his expression balanced precisely between embarrassment and genuine fear. "She said two days," he says. "She always says two days and she always means two days. It's been four. I wouldn't trouble you except — she's not the kind of person who gets lost."*

**Beat 1 — The Search.** Players head north. The ridge trail is clear for the first hour. Around the two-mile mark, environmental tells begin: no birdsong, fewer insects, a subtle wrongness that players with Arcana or Nature can identify as unnatural rather than threatening. The depression becomes visible from a low rise. So does Ren, sitting beside it with her back to them, writing.

**Beat 2 — Ren's Briefing.** Ren accepts water, acknowledges the players have been sent to find her, and immediately begins explaining the site. She does not sugarcoat: she ran out of water because she misjudged how long she would stay. She is not apologetic about the staying. She will share everything she knows — she has no reason not to — but she structures it as a cartographic lecture. Players who engage with her data rather than urging her to leave immediately get more of it.

**Beat 3 — The Question.** At some point Ren pauses and says, flatly: "This didn't exist six weeks ago. I surveyed this ridge in the autumn. There was nothing here." Beat. "I think it's connected to the Crown breaking. I can't prove that. I don't know enough about what the Crown does to prove it. But the timing is the same." She goes back to writing.

| DC | Skill | Outcome |
|----|-------|---------|
| 10 | Perception | Notice the harmonic resonance — a near-sound, felt more than heard |
| 12 | Arcana | Identify the site as a ley-line intersection, likely recently active |
| 14 | Investigation | Find the site without Ren's trail; also reveals two secondary nodes within fifty feet |
| 16 | Arcana | Recognize the activation pattern as consistent with a large-scale magical disruption event |
| 18 | History | Recall that ley-line reactivation was recorded once before — following the Sundering, six hundred years ago |

**Resolution Path A — Return with Ren.** Players convince Ren to come back now (water, food, the argument that she can return tomorrow). Ren agrees, but spends the walk back asking them questions about the Cracked Crown. She gives them her annotated maps the next morning.

**Resolution Path B — Stay and Document.** Players offer to help document the site. Ren works them like a field crew. They spend two to four hours there and return together. Ren's notes are more complete, and she gives the players a copy of the secondary node locations as a direct reward for useful assistance.

---

## IF IT FAILS

**If the players don't go:** Ren finds her own way back on day five, dehydrated and immediately fine. She gives nobody her notes. The ley-line site goes unreported, and a different Sentinel Order scout finds it eight weeks later — too late to connect it to the Crown fracture in the early window.

**If the site is reported to the wrong people:** A Cabal operative in Lighthaven intercepts the information before it reaches Elyndra. Ren's maps disappear from her workshop three days later. She notices immediately.

---

## REWARDS

- **Ren's annotated regional maps** — a complete set covering everything within forty miles of Lighthaven, more accurate than anything the Sentinel Order's library holds. Better travel times, hidden paths, and three locations marked as "survey anomalies" that Ren has been meaning to investigate.
- **Standing contact** — Ren is not warm, but she is reliable. If the players bring her cartographic data she'll trade maps and accurate regional intelligence. If they mention Elyndra's name she'll listen.
- **Information** — Ren's ley-line notes, which Elyndra will want immediately.

---

## CONNECTS TO

- **The Hungry Dark** — ley-line reactivation creates environmental disturbances that accelerate the Dark's movement patterns
- **The Cracked Crown** — Elyndra needs Ren's notes; this is the first physical evidence of infrastructure-level Crown damage
- **Any Lighthaven-based quest** — Ren's maps make future investigations in the region significantly easier
