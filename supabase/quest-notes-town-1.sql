# Who Remembered First
### *She has never left Thornwick. The memory is from somewhere else entirely.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Thornwick |
| **Level Range** | 2–5 |
| **Duration** | 45–90 minutes (single session) |
| **Quest Giver** | Elys (reluctant; she doesn't understand what she needs) |
| **Quest Type** | Town — Investigation, Pale Choir, Sensitive Roleplay |

---

## THE SITUATION

Elys is a Thornwick woman in her mid-thirties who has lived in this town her entire life. She works at the tannery. She has a garden. She is, by every measure, an ordinary person in an ordinary place. Three days ago she woke up with a memory that does not belong to her.

She has been trying to rationalize it. Perhaps a dream that felt real. Perhaps something she overheard years ago that surfaced strangely. But the memory is detailed in the way that genuine memories are: textured, sequential, with the specific sensory quality of lived experience. She is inside the Thornwick bell tower on the night it was silenced, and she is the person who placed the ward. She can feel the tools in her hands. She knows the words that were spoken. She is not Elys in the memory. But she is fully present in it.

She told no one for two days. On the third day she told her neighbor, who suggested she speak to travelers before she spoke to anyone official, because official attention in Thornwick lately has not felt entirely safe. The players are those travelers.

---

## BACKGROUND

*(DM-only — do not share with players)*

Elys is carrying a Pale Choir identity fragment — a partial absorption of a Cabal operative's memory and experiential record, delivered through proximity to a low-level Choir exposure event that occurred when the operative passed through Thornwick's outskirts approximately six weeks ago. The operative was scouting the area while the Choir's influence was temporarily extended by the bell tower's dormant warding.

The operative did not fully enter the Choir's range. The exposure was brief and partial. But it was enough for a fragment to transfer — not to the operative, but to the nearest susceptible receiver, which was Elys. She is not at risk of further absorption. This is not an ongoing condition. It is, however, a contained record of genuine intelligence.

The fragment contains three pieces of recoverable information:
1. The operative's face — specific enough to describe and eventually match to other Cabal sightings.
2. A Vaelthorn street address where the operative checks in — a specific building on the courier route used for Cabal communication.
3. A coded phrase repeated twice in the memory, used as a contact signal with operatives inside that building.

**Extraction methods:**

*Method A — Cleric Identify spell:* Surfaces the fragment cleanly and completely. However, the mental contact required causes a **DC 12 Wisdom saving throw** for Elys. On a failure, the shock of the identification process shatters the fragment and the memory is lost permanently. On a success, the fragment is readable and Elys retains access for follow-up questions.

*Method B — Patient questioning over several hours:* No spell required. No save required. Elys can access the memory voluntarily and describe it piece by piece. Takes significantly longer — two to four hours of careful, directed conversation — but Elys retains full access afterward and the fragment doesn't destabilize. The intelligence gathered this way is the same; the process is slower and requires the players to be present and attentive throughout.

The Pale Choir does not have an active interest in Elys. The operative who passed through does not know a fragment transferred. This is a recoverable intelligence asset, not an ongoing threat to her.

---

## KEY NPCs

**Elys** — Tannery worker, Thornwick resident, thoroughly ordinary in presentation. Has been not sleeping properly for three days. Describes the memory in fragments, stopping and starting, and apologizes periodically for having it — as if the memory is somehow a thing she did wrong. Needs someone to take this seriously without making her feel like she's in danger.

- **Appearance:** Work-worn clothes that she's been wearing too long. A stillness that reads as fear in someone whose default state is probably movement.
- **Manner:** Careful, precise about what she does and doesn't know, genuinely cooperative once she believes the players aren't going to dismiss her or report her somewhere frightening. The apologies are involuntary.
- **Wants:** For someone to tell her what is happening and whether it will stop. She does not want to be a significant person in a significant event. She wants to go back to the tannery.
- **Secret:** The memory has been getting more detailed, not less. She hasn't said this because she's afraid it means something worse than she already fears.

---

## RUNNING THE QUEST

