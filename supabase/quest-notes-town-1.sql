# The Halfling Quarter Argument
### *Two votes are already in. The third council member knows what's right. She needs a reason to say it.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Lighthaven, Halfling Quarter |
| **Level Range** | 1–5 |
| **Duration** | 45–75 minutes (single session) |
| **Quest Giver** | Elder Pell Goodbarrow, halfling community leader |
| **Quest Type** | Town — Negotiation, Research |

---

## THE SITUATION

Lighthaven's halfling quarter has maintained a communal garden on the same plot of land for sixty years. It is not large — half an acre of raised beds, fruit trees, and a small greenhouse the community built plank by plank in the year of the cold winter when the imports froze. It feeds roughly forty families through the lean months and functions as a community meeting point, a place where disputes get resolved and children get minded and the quarter takes care of itself without needing anything from anyone else.

The Meridian Trade Guild wants to build a warehouse on that plot. They have already secured two of Lighthaven's three council votes. The vote is in five days. Elder Goodbarrow has been making his case to the council, to the merchants, to anyone who will listen, with the devastating precision of a man who has been making formal arguments his entire life. He has been heard politely and dismissed. He approached the players because he has run out of legitimate options that he knows about, and he suspects there are ones he doesn't.

He is correct.

---

## BACKGROUND

*(DM-only — do not share with players)*

Lighthaven was chartered under a founding document that includes food security ordinances from its original settlement period — written in Old Aelorian in small handwriting on the back pages of the charter, which lives in the town library and has not been read completely by anyone currently serving on the council.

The relevant ordinance specifies that any action affecting a designated communal food supply requires unanimous council approval, not a simple majority. The halfling quarter garden qualifies as a designated communal food supply under the original charter's language — it was registered as such by Elder Goodbarrow's grandmother in the founding period, a fact that is also in the library records.

The property agent from the Meridian Guild is named Harwick Seld. He is smooth, professional, and patient. He has done this before in other towns and he has never lost. He is carrying significantly more cash than standard supply logistics negotiations require, which any player who pays attention will notice. His timeline matters more than his budget.

The third council member is Maret Beel. Her husband Dorris works as a mid-level factor for the Meridian Guild's Lighthaven operation. Her sister Hallie is halfling by adoption — raised in the halfling quarter, married into the community, has children who play in that garden. Maret has been publicly neutral. She is privately paralyzed. She will vote for what is right the moment she has a legal reason that protects her from being seen as voting against her husband's employer on personal grounds.

Finding the ordinance: **DC 12 History or Investigation** to locate the relevant section of the charter in the library. **DC 10** to translate the Old Aelorian once found. On a failed History check, a successful Investigation at the same DC finds a library index that points to the right section.

---

## KEY NPCs

**Elder Pell Goodbarrow** — three feet tall, impeccable green vest, silver pocket watch he does not consult but touches occasionally for composure. Has been presenting formal arguments in community settings for forty years. Delivers each sentence as if it is entering the record.

- **Appearance:** Immaculate despite everything. Polished boots. An expression of determined dignity.
- **Manner:** Formally precise. Not cold — he has warmth, it just comes out in careful language rather than expansive gestures. Occasionally funny in a dry way that takes a moment to register.
- **Wants:** For the garden to remain. More broadly: for the halfling quarter to have the same standing as every other part of Lighthaven, formally and in practice.
- **Secret:** He has already identified the charter ordinance himself. He is not a lawyer and does not trust his own reading enough to put it forward without someone credible confirming it.

**Maret Beel** — Council member, early fifties, speaks carefully and looks at the middle distance when weighing words. Does not fidget. Looks tired.

- **Appearance:** Council formal wear, slightly looser than it used to be. She's lost weight in the last month.
- **Manner:** Measured. Gives nothing away in public settings. In private, once she trusts that a conversation is actually private, she becomes direct.
- **Wants:** A legal reason to vote no. Not cover — a real reason that she can stand behind in front of her husband, the guild, and the quarter.
- **Secret:** She would have voted no on day one if her husband didn't work for the guild. This is not a secret she's keeping from herself.

**Harwick Seld** — The guild's property agent. Somewhere in his forties, well-dressed in the slightly-too-formal way of someone projecting legitimacy. Polite in all circumstances.

- **Appearance:** Expensive coat. Boots without a speck of mud despite the weather. A document case he carries everywhere.
- **Manner:** Professionally pleasant. Will not be flustered in a direct confrontation. Will negotiate upward on the cash offer rather than engage on legal grounds, because cash has worked before.
- **Wants:** To close this acquisition before the end of the week. His timeline is the tell.
- **Secret:** The warehouse is for a secondary purpose he isn't disclosing. He doesn't know what it is — he was hired to acquire the property, not briefed on the use.

---

## RUNNING THE QUEST

> *Elder Goodbarrow receives you in the garden itself — standing between two raised beds of winter greens, his watch in his hand for once, actually consulting it. "I have five days," he says. "I have made my argument to every person with authority in this town and been thanked for my time. I am not asking for sympathy. I am asking for help I don't know how to provide myself."*

**Beat 1 — Research.** Players go to the library. The town librarian, an older woman named Dessa, is helpful and slightly overworked. She knows where the charter is but hasn't read it herself. The Old Aelorian section takes a half hour to locate and work through. The ordinance is unambiguous once found.

**Beat 2 — Maret Beel.** Players need to bring this information to Maret before the vote. She is available but cautious — she'll listen in her home, not in any public setting. When she reads the ordinance she is quiet for a moment. Then she says: "This is real." Then: "Thank you." She does not thank them effusively. She means it.

**Beat 3 — The Vote.** Players can observe if they want. Maret presents the ordinance herself — she has clearly had a copy made and reviewed it thoroughly. The council chair looks uncomfortable. Harwick Seld, seated in the observers' gallery, makes a quiet offer of increased compensation. Maret says "that's not the applicable question" without looking at him. The vote is three to zero against.

| DC | Skill | Outcome |
|----|-------|---------|
| 10 | Investigation | Find the library's subject index for the charter |
| 12 | History or Investigation | Locate the food security ordinance section |
| 10 | Intelligence | Translate the relevant Old Aelorian passage |
| 13 | Insight | Recognize that Maret wants a reason, not persuasion |
| 15 | Investigation | Note Harwick Seld's cash surplus and realize the timeline pressure |

**Resolution Path A — Legal route.** Players find the ordinance, bring it to Maret, attend the vote. Clean outcome. The guild backs off. Harwick Seld is professionally gracious and immediately leaves Lighthaven.

**Resolution Path B — Direct confrontation.** Players confront Harwick Seld with the ordinance before the vote. He offers them money to not present it. If they decline and present it anyway, the vote outcome is the same. If they press him on the timeline, he reveals nothing but his discomfort is genuine and memorable.

---

## IF IT FAILS

**If the ordinance isn't found in time:** The vote passes. The guild begins surveying the plot within days. The halfling quarter loses the garden and forty families lose their food security buffer. Elder Goodbarrow thanks the players for trying, formally, and does not ask for further help.

**If Maret is approached wrong:** If players attempt to pressure or threaten her rather than provide legal grounds, she closes down entirely and votes with the guild — not because she wants to, but because she won't be seen as intimidated. The ordinance still exists; they can try the direct council presentation instead.

---

## REWARDS

- **Elder Goodbarrow's genuine gratitude** — and pie. Specifically, four different varieties delivered to wherever the players are staying, in a basket with a formal note. The pie keeps coming for as long as they're in Lighthaven.
- **The halfling quarter as a safe haven** — the community knows who helped. Information, food, shelter in a pinch, and a network of people who notice things and report them to Elder Goodbarrow.
- **Elder Goodbarrow as an information contact** — he has sixty years of Lighthaven history in his memory and shares it precisely when asked.

---

## CONNECTS TO

- **Succession Crisis** — the Lighthaven political landscape and which council members can be trusted
- **Any Loyalist network quest** — the halfling quarter becomes a reliable quiet space for sensitive meetings
- **Bell at Thornwick** — Elder Goodbarrow knew the previous bell-keeper's family; he has history
