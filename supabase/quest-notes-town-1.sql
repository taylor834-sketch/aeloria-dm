## IF IT FAILS

**If players attempt to break Andren out:** Eld cannot help them. If they succeed, Andren is free but hunted. Blackwell marks him as a deserter and the garrison increases scrutiny on all unusual travelers in Dunmore for the next month. Eld distances himself from the players permanently — he needed a clean solution.

**If the players don't act:** Andren is held for fourteen days and released with a formal demotion. He's no longer garrison. He's in Dunmore with no employment and a record. He's still the same person, still available to the players later, but the moment when his refusal might have meant something has passed.

