**Resolution Path A — The contract clause.** Clean, legal, sustainable. Andren is released under the clause. The requisition order is technically still in effect but Andren's refusal is no longer punishable. Eld folds the clause into garrison standing orders quietly. Other soldiers see it.

**Resolution Path B — Cover mission.** Andren is released on paper as assigned to external work. He's not a fugitive, but the fiction has to hold. Eld will maintain it on the log, but Andren's status is technically contingent on the mission being real. This works cleanly if the players actually bring Andren on something; it becomes awkward if they don't.

