## THE SITUATION

Three days ago, a Blackwell garrison soldier named Andren refused to participate in the grain requisition from Millhaven's western farms. This is not complicated behavior — Andren watched a family's winter grain stores loaded into military carts and said he wouldn't do the next one. He said this in front of other soldiers and his commanding officer. He was placed under garrison arrest within the hour. He has been there since.

His commanding officer is Captain Eld. Eld disagrees with the arrest. He also cannot say so publicly without implicating himself as insubordinate to the requisition order, which came from Blackwell command, which means it came from a political apparatus Eld is not in a position to openly challenge. He has been conducting himself with careful professional neutrality for three days while privately looking for any clean way to resolve this.

A mutual contact — not named, not present — suggested that travelers passing through Dunmore might be able to help in a way that Eld cannot help himself. Eld made no promises and has not committed to anything, but he is available to speak with if approached correctly.

---

## BACKGROUND

*(DM-only — do not share with players)*

Captain Eld is a Crown Loyalist in the early stages of active network building. He is not yet committed to any formal Loyalist structure — he has simply been identifying people he trusts and keeping his options open. Andren is one of those people. Eld can't sacrifice Andren to garrison politics and maintain the relationships he needs for what comes next.

The players have three viable approaches:

**Option 1 — Break Andren out.** Possible. The garrison cell is not a military prison — it's a converted storage room with a lock and a single bored sentry. Breaking him out creates fugitive status for Andren and Blackwell enmity for the players, and Eld cannot acknowledge or assist with this approach. He would be privately grateful but publicly forced to report it. Not recommended. Not impossible.

**Option 2 — Give Eld a cover reason.** Andren is "needed for a legitimate external mission." This requires a plausible mission — something Eld can put in the garrison log with a straight face. **DC 13 Persuasion** to present the cover story compellingly enough that Eld can use it. The mission doesn't need to be entirely fabricated; if the players are actually working on something that requires an additional person, that's better. Eld needs something that can survive a log audit.

**Option 3 — Find the legal precedent.** Dunmore's garrison was established under a mercenary contract rather than a standard military charter, because Dunmore's founders distrusted standing armies and negotiated specific limits on what garrison soldiers could be ordered to do. One of those limits: soldiers cannot be ordered to participate in actions that constitute civilian harm under the original contract's language, and grain requisition from residential stores qualifies. The relevant clause is in the garrison records — **DC 13 Investigation** to locate the specific passage, which is filed under the original contract attachments rather than the active orders manual, where no one looks.

Andren doesn't know about the contract clause. Eld didn't know about it either. If players find it, Eld reads it carefully, reads it again, and then says: "I'll need an hour with the garrison legal reference." He comes out of his office forty-five minutes later and begins the paperwork to release Andren under the clause.

---

## KEY NPCs

**Andren** — Blackwell garrison soldier, mid-thirties, not a philosopher or a revolutionary. A man who does his job and drew a line at a specific thing. He is not angry in his arrest cell — he knew what refusing would mean and he refused anyway. He's more tired than dramatic about it.

- **Appearance:** Garrison uniform, slightly rumpled from three days in a cell. Sits with his back straight anyway.
- **Manner:** Direct, unembellished. Doesn't explain himself at length because he doesn't think the explanation is complicated. "I wouldn't do it. I said so. Here I am."
- **Wants:** To not have this be for nothing. He's not asking to be rescued. He's asking that his refusal mean something.
- **Secret:** He has seen three other soldiers who seemed close to refusing. He didn't speak to them before his arrest. He wonders if they'll hold now that he didn't.

**Captain Eld** — Mid-forties, Blackwell garrison commander, the specific kind of military officer who became military because he believed in the institution and has spent twenty years watching the institution used for things he doesn't believe in. He is professionally neutral in all public-facing interactions. In private, careful. Not warm — practical.

- **Appearance:** Clean uniform that he maintains carefully. The kind of stillness that comes from long practice keeping reactions off his face.
- **Manner:** Every sentence measured before delivery. Does not waste words. Asks precise questions.
- **Wants:** A clean solution that protects Andren without making Eld publicly insubordinate to Blackwell command.
- **Secret:** He has been building a mental list of people he trusts for two years. The players are being evaluated for that list right now, whether they know it or not.

---

## RUNNING THE QUEST

> *You hear about the soldier before you find the contact. In Dunmore, the arrest is known — a garrison town keeps no secrets from itself. The detail that travels is not the arrest but the refusal: a Blackwell soldier said he wouldn't load grain from families' winter stores and said it out loud. In the garrison yard, in front of witnesses. People repeat this with the careful neutrality of people who have an opinion they're not saying.*

**Beat 1 — Finding Eld.** He doesn't come to the players; they have to find him. He's available for a conversation that looks, from the outside, like a routine traveler consultation with a garrison commander. He states the situation plainly and without embellishment. He does not ask for any specific approach — he tells the players what he needs (a clean resolution, no log violations) and waits.

**Beat 2 — Choosing the approach.** This beat is entirely player-driven. The garrison records are accessible in the public reference room. Andren can be spoken to through the cell door. Eld will answer direct questions honestly if they're asked in his office.

**Beat 3 — Resolution.** Whichever path is taken, the moment of resolution is Eld releasing Andren himself — unlocking the cell, not delegating it. He tells Andren: "You're being released. Do not make me regret the paperwork." Andren says nothing. He nods.

