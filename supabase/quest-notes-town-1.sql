# The Bell-Ringer's House
### *He just doesn't want to sell. That should be a simple thing.*

---

## AT A GLANCE

| Detail | Info |
|--------|------|
| **Region** | Aranthor — Thornwick |
| **Level Range** | 1–5 |
| **Duration** | 45–75 minutes (single session) |
| **Quest Giver** | Corwin, Thornwick bell-ringer |
| **Quest Type** | Town — Investigation, Confrontation |

---

## THE SITUATION

Corwin has tended Thornwick's bell tower for twenty-two years. He inherited the position from his father, who inherited it from his mother. The house adjacent to the tower — squat, stone, three rooms — has been in his family longer than anyone in Thornwick can remember. He sleeps there. He cooks there. He tends the bell from there. It is the only home he has ever had.

A property agent named Davan Cray approached him three weeks ago. Friendly, unhurried, representing "Blackwell supply chain expansion" — they needed the building as a relay station for courier logistics on the Vaelthorn road. Good price, full market value plus twenty percent. Corwin said no. Davan came back a week later with better terms. Corwin said no again. Davan came back this week with papers that described the property transfer as already pending, which it is not, and a figure that Corwin couldn't make sense of. Corwin doesn't have legal experience. He doesn't know if he can say no. He doesn't know who to ask.

He asked the players because they were the first people who stopped at the tower who seemed like they might know something he didn't.

---

## BACKGROUND

*(DM-only — do not share with players)*

Davan Cray does not work for Blackwell. He is a Shadow Cabal logistics operative. The house is wanted as a relay point — a fixed location on the direct courier route between Vaelthorn and Dunmore where Cabal messengers can safely stop, exchange information, and continue without attracting attention. The bell tower location is specifically useful because couriers can use the bell's ring schedule as a timing signal.

This is not the first such purchase. Players who investigate will find two other properties on the same route that were sold in the last three months: a farmhouse with road frontage eight miles east and a small inn on the Dunmore approaches that changed ownership and then had its common room "reserved for long-term tenants." The pattern, once visible, maps the Cabal's courier infrastructure directly.

Davan's timeline is real. He has been authorized to spend significantly more than he has offered — his instructions are to close the acquisition before the next new moon, which is eleven days away. If confronted with evidence that his other purchases are known, he will offer to triple the original price immediately rather than negotiate. His timeline is more valuable to him than any amount of money.

Corwin cannot simply be told this; he needs someone to tell him clearly that he does not have to sell, that the papers Davan showed him are not legally binding pre-transfer documents, and that Davan's pressure tactics are not legitimate. That is the first half of this quest. The investigation is the second.

Finding the pattern: **DC 14 Investigation** to locate both other properties through Thornwick's courier registry, property records, or road trader networks.

---

## KEY NPCs

**Corwin** — Thornwick's bell-ringer, older, unhurried in the way of people whose work is timed to bells rather than clocks. Tends the bell carefully, in the literal sense — he oils the mechanism, inspects the rope, checks the mounting brackets. The bell is his responsibility and he takes responsibility seriously.

- **Appearance:** Work-roughened hands, permanent slight squint from years of watching weather from the tower. A belt that belongs on a workman, not a formal interview.
- **Manner:** Gentle, a little formal, careful with words in the way of someone who knows he's out of his depth. Does not complain. States facts.
- **Wants:** To stay in his home. Also: to understand what's actually happening, because Corwin doesn't like not understanding things.
- **Secret:** The bell tower keeps notes — a log going back ninety years that records everyone who rang the bell for official town events, visitors who asked about the tower, and anything Corwin or his predecessors noted as unusual. One entry from eighteen months ago describes two "survey visitors" matching the general description of Shadow Cabal advance work.

**Davan Cray** — The property agent. Smooth, patient, carries more cash than legitimate supply logistics requires, and is dressed in the slightly-too-polished way of someone performing a professional role rather than inhabiting one. Not threatening in demeanor — the opposite, in fact. He speaks to Corwin with the patient warmth of a man who expects to get what he wants eventually.

- **Appearance:** Well-kept road coat. A document case that he opens with practiced efficiency. Steady eye contact.
- **Manner:** Patient. Will not be visibly rattled. Will increase the offer immediately when pressured rather than arguing the point.
- **Wants:** The property, closed, before the new moon. He is not personally invested in Corwin or the building; he is professionally invested in the timeline.
- **Secret:** He is not a property agent. He is a Cabal logistics coordinator. The building's intended use would end Corwin's access to the tower permanently, which Davan considers an acceptable collateral outcome.

---

## RUNNING THE QUEST

> *The bell tower is at the north end of Thornwick's main street, slightly apart from the other buildings. Corwin is in the yard beside his house when you find him, re-coiling a length of rope. He looks up with the expression of a man who has been worrying about something specific and has not found a way to stop. "Someone wants to buy my house," he says. "I don't want to sell it. I don't know if that's enough."*

**Beat 1 — The legal situation.** Corwin shows the players Davan's papers. A **DC 10 Investigation** reveals the papers are real legal forms — but blank in the final authority columns, meaning they are a template, not a completed transfer. Corwin has not signed anything binding. He does not have to sell unless he wants to.

**Beat 2 — The pattern.** Players who investigate further — talking to traders, checking the courier registry, asking about recent property sales on the Vaelthorn road — can find the other two purchases. The relay infrastructure becomes visible. A player with Cabal knowledge (**DC 12 Arcana or History for Sentinel Order background**) recognizes the acquisition pattern as consistent with Cabal logistics positioning.

**Beat 3 — Confrontation.** Players can confront Davan directly, present the pattern to him, or simply tell Corwin to refuse and see what Davan does. If confronted with knowledge of the other purchases, Davan offers triple the original price without hesitation — his tells are the speed of the offer and the size. He does not threaten. He does not lose his professional manner. He simply becomes more expensive.

| DC | Skill | Outcome |
|----|-------|---------|
| 10 | Investigation | Identify that the transfer papers are incomplete and non-binding |
| 12 | Insight | Recognize that Davan's patience is professional, not personal — this is a job |
| 14 | Investigation | Find the two other properties and map the relay corridor |
| 15 | Persuasion | Convince Davan to reveal who sent him (he won't give a name, but admits it isn't Blackwell) |
| 16 | Insight | Realize Davan's timeline is more valuable to him than any financial negotiation |

**Resolution Path A — Legal clarity.** Players inform Corwin he doesn't have to sell, confirm the papers are non-binding, and let Davan's deadline pass. The acquisition fails. Davan leaves Thornwick and does not return. The relay is routed through a different location. The pattern remains, but Corwin's home is safe.

**Resolution Path B — Expose the network.** Players bring the relay corridor map to the Sentinel Order. This gives Elyndra a new Cabal courier map and triggers a follow-up investigation into the other two properties. Higher impact, but also signals to the Cabal that their logistics pattern was identified.

---

## IF IT FAILS

**If Corwin is pressured into selling:** He signs the papers. Davan pays efficiently and leaves. Two weeks later, Corwin is "offered" lodging elsewhere — technically voluntary, practically not. The bell tower is accessible but the adjacent house is now staffed by Cabal couriers. The relay operates invisibly for months.

**If players miss the pattern:** The individual acquisition fails (they can still help Corwin) but the relay infrastructure continues via the other two buildings. The Cabal courier map remains unknown to the Sentinel Order.

---

## REWARDS

- **Corwin's relief** — expressed as a handshake and a brief, complete silence that contains more gratitude than words.
- **Thornwick historical knowledge** — Corwin knows this town and its history in the specific way of someone who has been watching it from a tower for twenty years. Reliable contact for anything involving Thornwick's past or present.
- **The bell tower log** — if players think to ask, Corwin shares it. Contains eighteen months of Cabal advance work recorded without being recognized.
- **The relay corridor map** — if players found the other properties, this is intelligence the Sentinel Order does not have.

---

## CONNECTS TO

- **Bell at Thornwick** — Corwin's history with the Cabal's interest in the bell; his tower log predates the warding event
- **Black Rose Thorns** — the relay corridor is part of the Cabal's Vaelthorn-Dunmore logistics infrastructure
